<h1>Conversa con nosotros en IRC</h1>

<p>Muchos de los desarrolladores y usuarios de Freenet pueden a menudo ser encontrados en el canal <a href="https://en.wikipedia.org/wiki/IRC">IRC</a>,
#freenet en chat.freenode.net. Casi todos en el canal son voluntarios, y 
pueden estar ocupados con otras cosas, así que quizas no tengas una respuesta inmediata: <b>Haz tu 
pregunta/di cual es el problema, luego espera unos pocos minutos, y alguien  
te ayudará</b>.</p>

