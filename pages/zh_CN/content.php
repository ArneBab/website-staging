      <h2>内容</h2>
      <h3>Freenet 是内容分布式系统</h3>
      
      <p>
	除了匿名外, Freenet 也是传播文件的一个有效方法. 作为分布式网络 Freenet 有以下优势:
	<ul>
	  <li>
	    <b>完全去中心化</b><br/>
	    信息可以插入 Freenet 供下载而不依赖任何中心服务器. 插入者需要做的只有将内容的 "键钥" 发布出去, 他人即可通过 "键钥" 下载内容.
	  </li>
	  <li>
	    <b>自适应的缓存</b><br/>
	    很多分布式内容网络架构只允许一个节点与其他节点分享已经下载的文件. 而 Freenet 节点中的自适应缓存信息可满足需求而无关节点已下载内容. 此特性允许 Freenet 比其他方案对流行文档更快速的 "扩张" 并增加负载平衡性能.
	  </li>
		
	  <li>
	    <b>強安全性</b><br/>
	    Freenet 长期支持的概念 "内容 Hash
	    键钥" (CHK) 保证了接收数据的完整性.此概念已被其他架构接受. Freenet 还支持 "子空间签名键钥" (SSK) 可对内容进行数字签名. 同样也可以保证了数据完整性, 而且比 CHK 更灵活.
	  </li>
	  
	  <li>
	    <b>转发失败纠错</b><br/>
	    与其他分布式内容网络架构一样, Freenet 部署了 "转发失败纠错", 可在文档某些部分无法接收时重建文档. 特别的, Freenet 还支持 "修复", 增强了文档重建能力和支持对缺失部分的重插入.
	  </li>
	</ul>
	本页连接包含在 Freenet 上的内容. 如果你想添加连接请 email <a href="mailto:ian@locut.us">我们</a> - 记住我们只能连接在美利坚合众国法律规定可合法传播的内容.
      </p>
