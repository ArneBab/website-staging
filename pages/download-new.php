<body><h2>Download Freenet 0.7</h2>

<h3>Important note for first time users</h3>

When you first start Freenet 0.7 your node will not know any other nodes on the network, you need to connect to other node. To find other node you should connect to the irc server <big>irc.freenode.com</big> and join the channel <big>#freenet</big>.
<p> <i>If you have a firewall or a NAT box click <a 
href="http://freenetproject.org/index.php?page=faq#firewall"><b>here</b></a> for some info.</i> <BR>
&nbsp;<BR>
<h3>Windows</h3>
- Run <big><a href="http://downloads.freenetproject.org/freenet-pre-07-alpha.exe">freenet-webinstall</a></big><br>
<br>
It will automatically install Freenet and other required components for you. When done, he launch your browser directly on Freenet. <br>
(Freenet contains <u><b>NO spyware or adware</b></u> , it's Free Software! The source code is publicly available for review) <BR>
&nbsp;<BR>
Freenet works best with <b>Windows 2000 Professional, XP Professional or NT. </b> Windows 95, 98 and 2000 Millennium Edition work rather
less well; in particular, the installer won't run on those operating systems.
<BR>
&nbsp;<BR>
<h3>Unix/Linux</h3>
You need to have a recent Java Runtime Environment. We have experienced best results with Sun's Java Runtime Environment which can be obtained from <a href="http://www.java.com/">http://www.java.com/</a>. <BR>
Java version 1.4.1 and later will work. However, be aware that there are applet security vulnerabilities in all versions prior to Java 5 update 4. Generally, we recommend using at least Java 1.4.2.
<P>- Run <big><a href="http://downloads.freenetproject.org/alpha/installer/freenet.jnlp">freenet-webinstall</a></big></P>
<P>If you encounter some problems try this : javaws http://downloads.freenetproject.org/alpha/installer/freenet.jnlp</P>
<P>After you start Freenet wait a few seconds for it to start-up (on a slow computer, you may need to wait about 30 seconds), and visit http://127.0.0.1:8888/ in your web browser to access Freenet's user interface. <BR>
&nbsp;<BR>
</P>
