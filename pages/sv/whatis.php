<h2>Vad är Freenet?</h2> 
<p>Freenet är fri programvara som låter dig publicera och skaffa information
på internet utan att rädsla för censurering. För att uppnå denna frihet är
nätverket helt decentraliserat och utgivare och konsumenter av information
är helt anonyma. Utan anonymitet kan det aldrig existera total yttrandefrihet,
och utan decentralisering är nätverket sårbart för attacker.</p>

<p>Kommunikationen på Freenets noder är krypterade och "routas" genom andra noder
för att göra det mycket svårt att avgöra vem som begär informationen och vad
innehållet är.</p>

<p>Användarna bidrar till nätverket genom att ge bandbredd och en del av sin
hårddisk (kallat "data store" (eng. datalager)) för att lagra filer. Till
skillnad från andra peer-to-peer-nätverk för fildelning, tillåter inte Freenet
sina användare att ha kontroll över vad som lagras i "data store". Istället 
behålls filerna eller tas bort beroende på hur populära de är, vilket innebär
att den minst populära filen tas bort för att ge plats åt mer populärt innehåll.
Filerna i datalagret är krypterade för att begränsa sannolikheten till åtal från
de personer som vill censurera innehållet på Freenet.</p>

<p>Nätverket kan användas till många olika saker och är inte begränsat till att
bara dela filer som andra peer-to-peer-nätverk. Det fungerar mer som ett Internet
inuti Internet. Till exempel kan Freenet användas för att:   


<ul>
<li>Publicera webplatser eller 'freesites'</li>
<li>Kommunicera via forum/BBS</li>
<li>Informationsdistribution</li>
<li>Skicka e-post</li>
</ul>

<p>
Till skillnad från många andra nydanande projekt har Freenet sedan länge kommit
ur laboratoriet. Det har laddats ner av mer än 2 miljoner användare sedan projektet
startades och det används för att sprida censurerad information runt hela världen,
inklusive länder som <a
href="http://freenet-china.org">Kina</a> och länder i mellanöstern. Idéer och koncept
i Freenet har haft ett stort genomslag i den akademiska världen. Uppsatsen "Freenet: 
A Distributed Anonymous Information Storage and Retrieval System" var den mest citerade
vetenskapliga rapporten år 2000 enligt <a href="http://citeseer.nj.nec.com/articles2000.html">Citeseer</a>
och Freenet har också inspirerat till uppsatser inom juridik och filosofi. Ian Clarke,
Freenets skapare och projektkoordinator, valdes till en av de 100 största uppfinnarna 2003 
av MIT's (Massachusetts Teknik Institut) tidning Technology Review.
