<h3> 3. Vikten av ett Fritt informationsflöde</h3>
<p>Yttrandefriheten är vanligtvis, i de flesta västerländska kulturer, ansedd att
vara en av de viktigaste rättigheterna en individ kan ha. Varför är friheten att
dela idéer och åsikter så viktig? Det finns många sätt att svara på den här frågan.</p>

<h3> 3.1 Kommunikation är det som gör oss till människor</h3>
<p>En av de mest uppenbara skillnaderna mellan människan om resten av djurriket är
vår förmåga att kommunicera sofistikerade och abstrakta koncept. Även om vi ständigt
upptäcker att djurs kommunikationsförmåga är mer avancerad än vi har trott tidigare,
är det osannolikt att något djur kan komma att jämföra sig med människans förmåga på
detta område.</p>

<h3>3.2 Kunskap är en god sak</h3>
<p>De flesta individer som får alternativen att veta något och att inte veta något, kommer
att välja att få mer information hellre än mindre. Krig har vunnits och förlorats beroende
på vem som var bäst informerad. Detta beror på att mer information låter oss ta bättre
beslut och förbättrar vår förmåga att överleva och vara framgångsrika.</p>


<h3>3.3 Demokrati förutsätter en välinformerad befolkning</h3>
<p>Många folk lever idag i demokratiska länder och de som inte gör det, vill förmodligen
göra det. Demokrati är svaret på frågan om hur man ska skapa ledare, men förhindra dem
att missbruka makten att leda. Den åstadkommer detta genom att ge befolkningen makten
att ändra regeringen genom att rösta. Dock innebär röstning inte per automatik att 
du lever i ett demokratiskt land. För att folket ska kunna styra sin regering effektivt,
måste den veta vad den gör, folket måste vara välinformerat. Detta är ett kretslopp,
kretsloppet kan brytas om staten/regeringen har makten att reglera vilken information 
som befolkningen har tillgång till.</p>


<h3>4. Censur och frihet</h3>
<p>Alla människor värdesätter sin frihet, vissa anser den till och med så viktig att
de är villiga att dö för den. Många vill gärna tro att de är fria att tänka och
ha vilka åsikter de vill, speciellt i västländerna. Överväg nu att någon hade möjlighet
att kontrollera informationen du har tillgång till. Detta skulle ge dem möjligheten att
manipulera dina åsikter genom att dölja vissa fakta för dig, genom att presentera lögner
och censurera allting som motsäger dessa lögner. Detta är ingen Orwellsk fantasi, detta
är det standardiserade tillvägagångssättet för de flesta västerländska statsmakter idag,
att ljuga för sin befolkning. Så ofta, att folk numera tar det för givet, även fast detta
faktum underminerar de demokratiska principer som legitimerar statens existens över huvud
taget.</p>

<h3>5. Lösningen</h3> 
<p>Den enda möjligheten för att säkerställa att en demokrati fortsätter att vara effektiv
är att säkerställa att staten inte kan kontrollera befolkningens möjligheter att dela med
sig av information, att kommunicera. Så länge allt vi ser och hör filtreras är vi inte 
helt fria. Freenets syfte är att låta två eller flera personer som vill dela med sig av
information, göra det.</p>

<h3>6. Är inte censur ibland nödvändig?</h3>
<p>Självklart är inte allt svart eller vitt, och det är många som känner att censur är en bra
sak under vissa omständigheter. Till exempel är det i många europeiska länder olagligt att
sprida rasistisk information. Stater försöker ofta hindra människor från att sprida och 
stödja idéer som ses som undergrävande för samhället. Det finns dock två svar på frågan ovan.</p>
<p>Det första är att man inte kan låta de med makt utöva "god" censurering, utan att de också
automatiskt får möjlighet att utöva "dålig" censurering. För att utöva någon form av censur
måste en stat ha möjligheten att övervaka och därmed också begränsa kommunikationen. Det
finns redan kritik mot att den anti-rasistiska censuren i många europeiska länder hindrar 
legitim historisk analys av händelser som Andra Världskriget.</p>
<p>Det andra argumentet är att "god" censurering är kontraproduktiv även om den inte glider över
till andra områden. Till exempel är det vanligtvis mer effektivt när man försöker övertyga någon
om något, att presentera motargumenten och bemöta dem. Genom att hindra människor att ta del av
de ofta sofistikerade argumenten rasister använder, gör man dessa människor sårbara för dessa 
argument när de till slut står inför dem.</p>

<p> Det första argumentet är givetvis det starkare av de två och är sant även om man inte håller
med om det andra. Helt enkelt, antingen har man censur, eller så har man det inte. Det finns 
inget mellanting.</p> 

<h3>7. Men varför är anonymitet nödvändigt?</h3>
<p>Man kan inte ha yttrandefrihet utan möjligheten att vara anonym. Majoriteten av censuren
är retrospektiv, det är vanligtvis mycket lättare att minska yttrandefriheten, genom att 
straffa de som utövar den, efteråt, än att hindra den innan den utövas. Enda sättet att
motverka detta är genom att förbli anonym. Det är en vanlig missuppfattning att man inte
kan lita på anonymt given information. Detta är inte nödvändigtvis sant. Genom att använda
digitala signaturer kan människor skapa en säker anonym pseudonym vilken människor över tid
kan lära sig lita på. Freenet använder sig av en mekanism som kallas "subspaces" för att 
tillhandahålla detta.</p>

<h3>8. Och vad händer med upphovsrätten?</h3>
<p>Uppenbart är att mycket av Freenets publicitet rört upphovsrätt, och därför kommer jag att
skriva om det i korthet. Kärnproblemet med upphovsrätt är att upprätthållandet av den kräver 
övervakning av kommunikation. Man kan inte garantera yttrandefrihet om någon övervakar
allt man säger. Detta är viktigt, de flesta människor inser inte eller lyfter inte frågan om
detta när upphovsrätt debatteras, så låt mig göra det alldeles klart:</p>
<p>Man kan inte garantera yttrandefrihet och samtidigt upprätthålla upphovsrättslagar.</p>
<p>Det är på grund av detta som Freenet, ett system designat för att skydda yttrandefrihet, 
måste motverka upprätthållandet av upphovsrätt.</p>

<h3>9. Men hur ska artister belönas för sitt arbete utan upphovsrätt?</h3>
<p>Först och främst, även om upphovsrätt var det enda sättet för artister att få betalt för
sitt arbete, skulle jag hävda att att frihet är viktigare än att ha professionella artister
(de som anser att vi i så fall inte kommer att ha någon konst eller musik förstår inte
mekanismerna bakom skapande: människor kommer alltid att skapa, det är en drift, frågan är
bara om de kan leva på det).</p>
<p>För det andra, det kan ifrågasättas om upphovsrätten är effektiv i skrivande stund. 
Musikindustrin är den mest högljudda antagonisten till förbättringar i kommunikationsteknologi,
men enligt många artister som skulle belönas av upphovsrätten, blir de inte det av industrin.
Istället har upphovsrätten gjort att distributionskanalerna kunnat styras av mellanhänder, till
förlust för både artisterna och allmänheten.</p>
<h3>10. Alternativ till upphovsrätten</h3>
<p>Lyckligtvis kommer det aldrig att gå så långt. Det finns många alternativa sätt att belöna
artister. Den enklaste är frivillig betalning. Detta är en förlängning av mecenat-systemet som
flitigt använt för att belöna artister innan upphovsrätten, där en välbärgad person finansierade
en persons uppehälle för att denne skulle kunna skapa på heltid. Internet tillåter en intressant
utökad version av detta, där artister istället för att ha en enda välbärgad mecenat kan ha hundra
eller tusentals, som bidrar med små summor över Internet.</p>
<p>Vi praktiserar vad vi pläderar också. Den 15:e mars 2001 började Freenet-projektet ta emot 
donationer och inom en vecka hade vi samlat ihop över 1000 dollar.</p>
<h3>11. Mer sofistikerade tillvägagångssätt: Fairshare</h3>
<p>Självklart skulle vissa anse idén löjlig baserat (gissningsvis) på att ingen skulle betala
för något om de inte var tvungna (trots gott om bevis för motsatsen). Även fast jag avviker
från den något deprimerande människosyn, finns det mer sofistikerade mekanismer som tilltalar
människors egenintresse, till exempel "Fairshare", där man kan investera i en artist på liknande
sätt som en riskkapitalist investerar i en idé de gillar, och om den artisten är framgångsrik
belönas de i proportion till sitt ingångsbidrag. Detta har den trevliga bieffekten att uppmuntra
människor att ge mer pengar till obskyra artister som de tror har potential. Om deras investering
inte betalar sig, så har de fortfarande tillfredsställelsen av att ha bidragit till en artist
vars arbete de gillar.</p>
