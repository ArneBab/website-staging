<h3>1. Ett klargörande</h3> 
<p>Det finns många anledningar till varför 
människor involverar sig i Freenet-projektet. Vissa delar åsikterna
i det här dokumentet, andra delar varianter av dessa åsikter, vilka
också uppnås genom det vi försöker uppnå och ytterligare andra gillar
bara den tekniska utmaningen. Detta är idéerna som motiverade mig att
bygga systemet, men inte nödvändigtvis de åsikter som alla inblandade
i Freenet-projektet instämmer i.</p>

<h3>2. Föreslagen läsning</h3>
<p>För att detta dokument ska förstås, bör du förmodligen veta vad Freenet är. 
