<h3>Freenet-Architektur</h3>
<p>
Um sich mit dem Freenet zu verbinden müssen Sie Knoten-Referenzen mit anderen
Benutzen von Freenet austauschen. Die Referenzen müssen auf beiden Seiten
hinzugefügt werden um eine Verbindung herzustellen. Sie müssen also ihre/seine
und sie/er ihre Referenz hinzufügen.
</p>
<p>
Sie sollten mindesten drei Knoten haben, die mit Ihnen verbunden sind, idealer
Weise mindestens fünf bis sieben. Die Knoten die direkt mit Ihnen verbunden sind,
sind die einzigen Knoten im Freenet, die sehen könnten welcher Datenverkehr
Ihren Freenet-Knoten passiert. Sie sind auch die einzigen Knoten die Sie im
Netzwerk sehen können, die anderen sind vor Ihnen versteckt.
</p>
