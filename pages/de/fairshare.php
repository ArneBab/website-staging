  <p>
  Nicht viel, außer dass die Größe der Resonanz die wir in der Presse bezüglich
  des Effekts von Freenet auf das Urheberrecht bekamen, uns dazu gebracht hat
  über dieses Problem nachzudenken.
  </p>
  
