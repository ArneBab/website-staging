<h2>Inhalte</h2>
<h3>Freenet als System zur Verbreitung von Inhalten</h3>
<p>
Zusätzlich zu seinen Anonymitäts-Funktionen ist das Freenet auch eine
effektive Art große Dateien zu verteilen - welches oftmals höhere
Download-Geschwindigkeiten als andere Systeme zur Verbreitung von Inhalten
erreicht.
Freenet als Netzwerk zur Verbreitung von Inhalten (CDN) hat die folgenden
Vorteile:
</p>
<ul>
<li><b>Vollständig dezentralisiert</b><br>
Informationen können zum Download in das Freenet eingefügt werden ohne auf einen
zentralen Server angewiesen zu sein. Alles was die einfügende Person tun muss
ist, den &bdquo;Schlüssel&ldquo; zum Inhalt weiterzugeben und sie werden in
der Lage sein ihn herunterzuladen.</li>
<li><b>Adaptives Zwischenspeichern</b><br>
Viele CDN-Architekturen erlauben es nur Partnern welche die Datei schon
heruntergeladen haben, diese mit anderen Partnern auszutauschen. Freenet wird
um Anfragen, unabhängig davon was dieser Partner heruntergeladen hat,beantworten
zu können Informationen auf Partnern adaptiv zwischenspeichern. Dies ermöglicht
es Freenet, schneller als die meisten Lösungen für beliebte Dateien,
&bdquo;hochzuskalieren&ldquo; und verbessert die Lastverteilung.</li>
<li><b>Hohe Sicherheit</b><br>
Freenet unterstützt seit langem das Konzept der &bdquo;Content Hash Keys&ldquo;
(der Schlüssel der Datei ist die Prüfsumme des Inhalts) welches die
Unversehrtheit der abgerufenen Daten garantiert. Dieser Ansatz wurde seitdem
von anderen Architekturen übernommen. Freenet unterstützt auch &bdquo;Signed
Subspace Keys&ldquo; (signierte Unterraum-Schlüssel) welche es ermöglichen
den Inhalt digital zu signieren. Diese garantieren auch die Unversehrtheit der
Daten, sind aber flexibler als CHKs.</li>
<li><b>Vorwärtsfehlerkorrektur</b><br>
Genau wie manch andere CDN-Architektur benutzt Freenet
&bdquo;Vorwärtsfehlerkorrektur&ldquo; (FEC) welche es selbst dann ermöglicht
Dateien zu rekonstruieren, wenn Teile der Dateien nicht abgerufen werden können.
Einzigartig ist, dass Freenet auch das &bdquo;Heilen&ldquo; unterstützt, welches
die Rekonstruktion und das Wieder-Einfügen von fehlende Datei-Teilen beinhaltet.</li>
</ul>
