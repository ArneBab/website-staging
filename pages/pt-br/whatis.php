      <h1>O que é Freenet?</h1>
      <blockquote>
	<i>"Eu me preocupo com minha filha e a Internet o tempo inteiro, mesmo sendo ela ainda muito jovem para estar conectada. Eis o que me preocupa. Preocupa-me que daqui a 10 ou 15 anos, ela virá até a mim e dirá 'Papai, onde você estava quando eles tiraram a liberdade de imprensa da Internet?'"
	</i><br/>
	--Mike Godwin, <a href="https://www.eff.org/">Electronic Frontier Foundation</a>
      </blockquote>
      <p> 
	Freenet é um software livre que permite que você anonimamente compartilhe arquivos, navegue 
	e publique "freesites" (websites acessíveis apenas através da Freenet) e converse 
	em fóruns, sem medo de censura. Freenet é descentralizado para torná-lo 
	menos vunerável a ataque, e se usado no modo "darknet", onde usuários 
	conectam-se apenas a seus amigos, torna-se muito difícil de ser detectado.
      </p>
      
      <p>
	Comunicações através de nós Freenet são criptografadas e são roteadas através de outros nós para tornar extremamente difícil determinar
	quem está requisitando a informação e qual é o seu conteúdo.
      </p>

      <p>
	Usários contribuem com a rede dando largura de banda e uma parte de
	seus discos rígidos (chamado de "armazenamento de dados") para armazenar arquivos. 
	Os arquivos são automaticamente mantidos ou excluídos dependendo de quão populares são, com o menos popular
	sendo descartado para dar lugar a um conteúdo mais recente ou mais popular. Os arquivos são criptografados, de
	modo geral, o usuário não pode facilmente descobrir o que está armazenado, e esperamos que não possa ser
	responsabilizado por conta disso. Fóruns de discussão, sites e funcionalidade de pesquisa, são todos construídos
	 sobre este armazenamento de dados distribuidos.
      </p>
      <p>
	Freenet já foi baixado mais de 2 milhões de vezes desde que o projeto começou, e é utilizado para a distribuição
	de informações censuradas em todo o mundo incluindo países como a
	<a href="http://freenet-china.org">China</a> e o Oriente Médio.  Ideias e conceitos pioneiros na Freenet têm tido
	um impacto significativo no mundo acadêmico.
	Nosso paper 2000 "Freenet: A Distributed Anonymous Information Storage and Retrieval System" (Freenet: Um Sistema
	Anônimo de Distribuição, Armazenamento e Recuperação), foi o mais citado dos 2000, de acordo com o Citeseer, e
	Freenet também inspirou papers nos mundo do direito e da filosofia. 
	Ian Clarke, o criador do Freenet e coordenado do projeto, foi selecionado com um dos 100 mais inovadores de 2003
	pela revista MIT's Technology Review.
      </p>
      
      <p>
	Um importante desenvolvimento recente, que poucas outras redes possuem, é a "darknet"
	: Por conectar-se apenas a pessoas em quem confiam, os usuários podem reduzir significativamente 
	sua vulnerabilidade, e ainda se conectar a uma rede global através de amigos de seus amigos e assim
	por diante. Isso permite que as pessoas façam uso do Freenet mesmo em lugares onde Freenet seja
	ilegal, tornando muito mais difícil para os governos bloqueá-lo, e não depende de encapsulamento para o "mundo livre".
      </p>
      
