	<h1>Pessoas</h1>
	<p><b>Nota:</b> Esta é uma lista incompleta, um grande número de pessoas tem contribuido com o projeto. </p>
	
	<h5>Ian Clarke</h5>
	<p>
	  Freenet é baseado no paper de Ian, intitulado "A Distributed Decentralised Information
	  Storage and Retrieval System".  Ian começou o Projeto Freenet por volta de julho
	  1999, e continua a coordená-lo.  Em seu trabalho diário, Ian é o fundador e CEO da <a href="http://sensearray.com/">SenseArray</a>.
	</p>
	
	<h5>Matthew Toseland</h5>
	<p>
	  Matthew vem trabalhando no Freenet desde antes do lançamento do 0.5.  Seu trabalho,
	  e o dos outros, resultou em melhorias dramáticas para o desempenho e estabilidade da rede.
	</p>

	<h5>Oskar Sandberg</h5>
	<p>
	  Oskar foi um dos primeiros a contribuir para o projeto Freenet, e
	  tem feito alguns avanços teóricos importantes que dão início à Freenet 0.7, consulte a página de papers.
	</p>

	<h5>Florent Daigni&egrave;re</h5>
	<p>
	  Desde 2003, Florent melhorou vários aspectos do software e e realizou o projeto de administração do sistema. 
Em seu trabalho diário, ele é o Diretor Técnico da <a href="https://www.trustmatta.com">Matta Consulting</a>, uma empresa de
	  consultoria e segurança.
	</p>

	<h5>Scott Miller</h5>
	<p>
	  Scott é o responsável pela implementação de grande parte dos elementos de criptografia no Freenet.
	</p>

	<h5>Steven Starr</h5>
	<p>
	  Steven contribui com a administração da Freenet Project Inc, e é um consultor do projeto para negócios e publicidade.
	</p>

	<h5>Michael Rogers</h5>
	<p>
	  Michael tem principalmente contribuido com as simulações detalhadas como parte do Google 
	  Summer of Code. Ele tem sido muito útil na concepção da  <a href="https://old-wiki.freenetproject.org/NewTransportLayer">nova
	    camada de transporte</a>.
	</p>

	<h5>Dave Baker</h5>
	<p>
	  A principal contribuição de Dave tem sido o <a href="freemail.html">Freemail</a>, 
	  Seu projeto do Summer of Code para construir um funcional sistema de email-sobre-freenet, bem como alguma depuração 
	  em vários locais.
	</p>

	<h5>Robert Hailey</h5>
	<p>
	  Robert ajudou a melhorar a velocidade e a segurança do Freenet encontrando dois
	  <b>grandes</b> bugs, e recentemente contribuiu com algum código.
	</p>

	<h5>David Sowder</h5>
	<p>
	  David (Zothar) tem ajudado o projeto Freenet direto, conforme a disponibilidade de tempo e interesse
	  , incluindo configuração, estatística e gestão de nó via FCP, a página de status do FProxy e as Mensagens de Nó para Nó (N2NM/N2NTMs).
	</p>

