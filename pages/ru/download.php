      <h3>Аппаратные требования</h3>
      <p>
    Как правило процессора c тактовой частотой 1ГГц и 1ГБ оперативной 
    памяти вполне достаточно. Фринет будет работать и на более слабой 
    технике. Как минимум потребуется 128МБ оперативной памяти, но 
    когда система не будет занята чем-нибудь еще ПО Фринет будет 
    использовать 512МБ и более. С процессором все проще. Известны 
    случаи запуска клиента Фринет на процессорах Pentium II 400МГЦ и 
    ATOM. С такими медленными процессорами загрузка и отображение 
    контента занимают больше времени.</p>
    
      <p>
    Фринет будет использовать некоторое место на вашем жестком диске 
    для хранения данных. Вы можете настроить объем хранилища от 100МБ, 
    но мы рекомендуем выделить для Фринет не менее 1ГБ, поскольку 
    Фринет использует хранилище данных для ваших загрузок. Фринет 
    также требует место для своей работы примерно 192МБ плюс 1МБ за 
    каждые 2ГБ хранилища.
      </p>
      
      <p>
      Для работы в Windows х64 потребуется установить 32-разрядную виртуальную машину Java 
      из-за ограничений <a rel="nofollow" href="http://wrapper.tanukisoftware.org/doc/english/download.jsp">Java Service Wrapper</a>.
      Она необязательно будет обновлять себя автоматически, поэтому, возможно, вам время от времени потребуется <a rel="nofollow" href="http://www.java.com/en/download/manual.jsp">обновлять программное обеспечение вручную</a>.
      </p>
      
      <h3>Обновление</h3>
      
      <p>
  Фринет обеспечивает механизм обновления через Фринет. Клиент Фринет 
  автоматически поддерживает актуальную версию, обновляясь с других нод-узлов 
  сети. Клиент Фринет будет корректно работать даже в случае разных версий с 
  подключенным нод-узлом. Обновление через Фринет анонимно и безопасно, и мы 
  рекомендуем использовать именно этот способ. Однако, если в системе что-то 
  будет серьезно нарушено, вы всегда сможете обновить свой нод-узел вручную 
  с наших серверов.</p>
      
      <ul>
  <li>Пользователи Windows могут запустить обновление ПО Фринет до последней 
  версии через меню в системном трее или запустив на исполнение файл 
  «update.cmd» из директории, с установленным ПО Фринет.</li>
  <li>Пользователи Mac и Linux могут обновить ПО Фринет, исполнив скрипт 
  «update.sh» из каталога с установленным ПО Фринет.</li>
      </ul>
      
