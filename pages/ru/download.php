      <h3>Итак, все работает. Что мне делать?</h3>
      
      <p>
  Когда установщик закроется, в браузере откроется страница с мастером 
  первого запуска. Здесь настраиваются основные параметры и теперь можно 
  использовать Фринет. Вы всегда можете получить доступ к Фринет позже 
  через меню в системном трее (внизу справа на экране), либо с помощью 
  ярлыка Browse Freenet на рабочем столе или через меню «Пуск». Также 
  можно открыть адрес 
  <a rel="nofollow" href="http://127.0.0.1:8888/" rel="nofollow">http://127.0.0.1:8888/</a> в вашем веб-браузере.
      
      <p>
  Для большей безопасности следует использовать отдельный браузер для 
  Фринет, предпочтительно в режиме конфиденциальности. В Windows, 
  клиент Фринет будет пытаться использовать Chrome в режиме инкогнито, 
  если такое возможно. IE плохо работает с Фринет, а Firefox и Opera 
  широко используются.
      </p>
      
      <p>
  Если вы знаете кого-нибудь, кто использует Фринет, вы можете улучшить вашу безопасность 
  и помочь построить надежную сеть, связав ваши узлы. Для этого откройте страницу
  <a rel="nofollow" href="http://127.0.0.1:8888/addfriend/">Добавить друга</a>. Вы и ваш 
  друг должны загрузить нод-ссылку друг друга. Отправить файл другому участнику сети, а 
  также добавить свою нод-ссылку можно с помощью формы в нижней части страницы. Когда обе 
  стороны добавят нод-ссылки друг друга, узел вашего друга будет отображаться на странице 
  «Друзья» со статусом «Подключен» или «Занят». Вы можете задать имя для вашего нод-узла 
  на странице конфигурации, чтобы вашим друзьям было проще понимать, что это именно вы. 
  Добавляйте в друзья только людей, 
  <b>которых вы реально знаете</b>. Добавление чужих в друзья вредит производительности 
  сети и не способствует ее безопасности (они могут быть плохими парнями!).
      </p>
      
