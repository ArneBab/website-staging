    <h3>Зеркальная установка</h3>

    <p>
      Если у вас есть директория с установленным рабочим ПО Фринет, которая 
      получилась путем копирования ее с другого компьютера (например rsync 
      или unison), не сложно сделать из нее зеркальную установку. Фринет не 
      беспокоится об IP-адресе своего хоста; иначе Фринет не работал бы
      на устройствах, которые получают каждый раз новый IP-адрес из DHCP пула.
    </p>

    <p>
      Все, что требуется сделать - сказать системе, в которую вы зеркально 
      устанавливаете ПО Фринет, чтобы она автоматически запускала демона 
      Фринет при загрузке. Выполните <tt>crontab -l</tt> в исходной системе, 
      найдите строку с пометкой "FREENET AUTOSTART" и добавьте ее в расписание
      демона cron в системе, куда выполняем зеркальную установку.
    </p>
    
    <p>
      Замечание. Каждая установка имеет уникальный ключ-идентификатор, 
      сгенерированный во время инсталляции. Если вы попытаетесь запустить
      два экземпляра Фринет с одинаковыми ключами-идентификаторами <em>в 
      одно и то же время</em>, оба Фринет-прокси-демона каждый в своей 
      операционной системе смутятся и расстроятся. Не делайте так.
    </p>

