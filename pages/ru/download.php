<h1>Загрузить Фринет</h1>
      
      <h3>Важное примечание для новых пользователей</h3>
      <p>
    Для лучшей производительности программное обеспечение Фринет будет 
    работать постоянно. ПО Фринет не должно мешать вам пользоваться 
    компьютером, поскольку требует около 200МБ оперативной памяти и 10% 
    одного из ядер процессора, и некоторое количество обращений к диску. 
    Мы настоятельно рекомендуем вам отключать клиент Фринет, когда вы 
    играете в компьютерные игры. В Windows вы можете сделать это с 
    помощью значка в системном трее, в других операционных системах 
    используйте ссылки в системном меню или на рабочем столе.
      </p>
      <p>
    По умолчанию клиент Фринет будет работать в обычном режиме 
    «просто работаю», автоматически подключаясь к другим нодам (Чужие). 
    Однако, если вы знаете несколько человек, которые уже используют 
    Фринет, вы можете включить режим высокой безопасности и 
    <a rel="nofollow" href="http://127.0.0.1:8888/addfriend/">добавить их в друзья</a>.
    В таком случае ваш клиент Фринет будет устанавливать соединение 
    только с друзьями, делая ваше пользование сетью Фринет практически 
    невозможным для обнаружения. В то же время вы будете иметь доступ к 
    остальной сети через друзей своих друзей и так далее. В таком 
    режиме сеть будет работать медленнее, пока вы не добавите 10+ 
    друзей, которые обычно онлайн в то же время что и вы.
      </p>

