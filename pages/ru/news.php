  <a name="sorry-again-1253"><h3>14 июня 2010 – Серьезная ошибка! Пожалуйста, обновитесь немедленно!</h3></a>
  <p>Билд 1253 исправляет очень серьезную ошибку. Все пользователи должны обновиться как можно скорее, 
  особенно те, кто использует 32-разрядные х86 системы. Это обновление «принудительно», так как 
  обновленный клиент Фринет не сможет подключаться к нод-узлам, которые используют версию младше 1252. 
  Ошибка содержалась в родной (C, не Java) библиотеке FEC. Эта библиотека используется для ускорения 
  SplitFile декодирования (этап загрузки, связанный с повышением шансов сохранить восстановимый файл 
  большого размера. На данном этапе в файл вставляются специальные проверочные блоки или избыточные 
  данные, в случае когда некоторые из оригинальных блоков данных не могут быть найдены). Это вызывает 
  вылеты на 32-разрядных системах c операционной системой Windows и, возможно, на 32-разрядных системах 
  с Linux тоже. В «диком виде» оно проявляется таким образом, что клиент нод-узел Фринет вылетает при 
  получении специфического служебного сообщения. Мы не знаем, можно ли развить этот баг во что-нибудь 
  более серьезное, поэтому сейчас мы отключили все родные FEC ускорители. ПО Фринет будет работать 
  несколько медленнее при декодировании загруженных файлов, но не будет падать. Мы будем рады помощи 
  с аудитом используемого кода библиотеки, если какие-нибудь C/JNI гуру смогут найти свободное время. 
  Скорее всего проблема случается на стыке JNI с кодом библиотеки FEC.</p>
  <p>Другие изменения в билде 1251/1252/1253 несколько более обнадеживающие:</p>
  <ul><li><b>SplitFile делит</b> большие файлы на блоки по 32КБ. Затем такие блоки группируются в сегменты
   размером до 4МБ. До билда 1251 все сегменты кроме последнего были одинакового размера ровно 4МБ, а 
   последний сегмент почти всегда был меньшего размера. В некоторых случаях он состоял лишь из одного 
   блока или из нескольких блоков. Из-за этого некоторые загрузки застревали, когда оставалось загрузить 
   всего несколько блоков. Сейчас все сегменты имеют одинаковый размер, есть и другие хитрости. К 
   сожалению, это было вызвано описанной выше ошибкой.</li>
  <li><b>Новый фильтр контента</b> инфраструктуры Фринет различает типы данных; например для HTML он 
  обеспечивает, что они не точно не отдадут ваш IP-адрес. Один из наших студентов Спенсер (Spencer) в рамках 
  программы Summer of Code работал над улучшениями в этой области. Далее эти улучшения объединили с первой 
  частью его работы. Сейчас разбор контента происходит на более низком уровне и поэтому может быть 
  использована клиентами FCP и для постоянной загрузки. Это также открывает путь для эффективной работы с 
  файлами большого размера, когда Спенсер реализует поддержку мультимедийных форматов позже в этом году.</li>
  </ul>
  <p>Люди, извините за случившееся. Сеть будет вести себя несколько хаотично в течение нескольких дней и 
  может работать медленнее чем обычно из-за «принудительного» обновления. Кажется, сейчас большинство 
  нод-узлов уже обновились. Спасибо за ваше терпение. Вы можете увидеть полный отчет 
  в <a href="http://amphibian.dyndns.org/flogmirror/">блоге Жабы (Toad)</a>.</p>

  <a name="sorry-folks-1248"><h3>8 июня 2010 - Ошибка «freenet.ini is missing» и билд 1249</h3></a>
  <p>Вчера сломалось много нод-узлов, поднятых под ОС Windows, в связи с ошибкой, которая выводила сообщение 
