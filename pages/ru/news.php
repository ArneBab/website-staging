  Windows о наличии новых обновлений. Для пользователей Linux и Mac OS/Х ситуация 
  сложнее, им придется ждать обновление Java для своих операционных систем.</p>
  
  <p>Для получения дополнительной информации, смотрите <a href="http://www.cert.fi/en/reports/2009/vulnerability2009085.html">здесь</a>.
