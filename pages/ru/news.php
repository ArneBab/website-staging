  <a name="more-peers"><h3>21 августа 2009 – Фринет повышает лимит подключений (№1 в UserVoice)</h3></a>
  <p>Версия 0.7 билд 1231 решает <a href="https://freenet.uservoice.com/pages/8861-general/suggestions/93039-release-the-20-nodes-barrier?ref=title">
  самый популярный вопрос</a>, поднимаемый пользователями в UserVoice. Прежде большинство нод-узлов 
  имело ровно 20 соединений. Теперь количество подключений (OpenNet пиры и подключения к Чужим) будет 
  меняться в зависимости от пропускной способности вашего исходящего канала в диапазоне между 10 и 40. 
  Таким образом медленные нод-узлы будут иметь меньше подключений (немного потеряв в эффективной 
  маршрутизации, увеличивается эффективность в целом), а быстрые – больше. Мы ожидаем, что данное 
  изменение повысит скорость; такое сложно симулировать, поэтому давайте узнаем на деле!</p>
  
