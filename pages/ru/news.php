      <a name="freenet-0-7-5-released"><h3>12 июня 2009 – Релиз 0.7.5 ПО Фринет</h3></a>
      <p>
  Проект Фринет рад объявить о выпуске программного обеспечения Фринет версии 0.7.5.
      </p>
      <p>
  Фринет – бесплатное программное обеспечение, разработано чтобы 
  предоставить возможность свободного обмена информацией через 
  сеть Интернет, не опасаясь цензуры или репрессий. Для достижения 
  этой цели Фринет делает очень сложным для противника раскрытие 
  идентичности (анонимной цифровой личности), авторов публикаций, 
  потребителей контента. Проект Фринет стартовал в 1999 году, в 
  марте 2000 вышла версия Фринет 0.1, до сих пор в стадии активной 
  разработки.
      </p>
      <p>
  Фринет несколько необычен тем, что вы можете опубликовать 
  контент в сеть и затем отключиться от сети. Ваш контент при этом 
  останется доступным для других пользователей Фринет; хотя со 
  временем он может быть удален, если никто не будет проявлять к 
  нему интерес. Фринет будет копировать и перемещать контент 
  внутри сети в зависимости от спроса, что делает удаление 
  контента из сети очень трудным для противника. Фринет будет 
  автоматически создавать больше копий популярного контента, чтобы 
  он всегда был доступен.
      </p>
      <p>
  Версия Фринет 0.7 представила концепцию «даркнет», позволяя 
  пользователям устанавливать подключения только со своими верными 
  друзьями (а через них и друзей их друзей – ко всей сети), что 
  значительно снижает уязвимость пользователей для атак. Вы можете 
  использовать Фринет даже если не знаете других пользователей 
  сети, просто это не будет настолько же безопасно.
      </p>
      <p>
  В версии 0.7.5 значительно улучшены производительность и 
  удобство использования, также улучшены безопасность и надежность. 
  В особенности:
      </p>
      <ul>
  <li>
    Теперь Фринет <a href="http://google-opensource.blogspot.com/2009/05/improving-freenets-performance.html">использует 
    базу данных</a> для хранения долгосрочных данных, которые 
    должны выжить после перезагрузки. Это увеличивает скорость 
    работы Фринет и снижает используемое программным обеспечением 
    количество памяти. В частности, сейчас вы можете иметь 
    практически любое количество одновременных загрузок и закачек, 
    не беспокоясь об использовании памяти.
  </li>
  <li>
    Доработан веб-интерфейс, чтобы было понятнее, что вы можете 
    делать в сети Фринет; отображается прогресс загрузки для 
    страниц и файлов, загрузка которых займет более нескольких 
    секунд; интегрированный в обзорную страницу поиск по сети. В 
    целом повышено удобство использования во многих областях.
  </li>
  <li>
    Значительно увеличены производительность вставки и получения 
    файлов и особенно страниц, а также ускорено начальное 
    подключение клиента Фринет к сети.
  </li>
  <li>
    Новый установщик для Windows работает с Vista так же хорошо, 
    как и с XP/2000 (Фринет также работает на Mac и Linux 
    системах).
  </li>
  <li>
    Много разных оптимизаций.
  </li>
  <li>
    Много-много исправленных ошибок!
  </li>
      </ul>

