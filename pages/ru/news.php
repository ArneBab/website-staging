  <a name="build1226"><h3>30 июля 2009 – Различные улучшения безопасности и подготовка файлового обмена с использованием фильтра Блума в новой стабильной сборке</h3></a>
  
  <p>
  Билд 1226 доступен и содержит много изменений. Существующие нод-узлы сети Фринет 
  будут обновляться автоматически. Некоторые из наиболее интересных нововведений:</p>
  <ul>
  <li><b>Окончательно исправлена <a href="http://www.theregister.co.uk/2005/05/13/freener_not_so_anonymous/">атака The Register’s</a>!</b><br>
  В 2005, The Register обнаружил, что многие из нас знали – Фринет кэширует все, 
  что вы посещали, в вашем хранилище данных. Если ваш компьютер был изъят или 
  украден, плохие парни могли определить, что и где вы смотрели. Или они могли 
  сделать это даже удаленно, зондируя хранилище данных. Сейчас эта проблема 
  решена. Клиент Фринет не хранит ваши посещения, а также увиденное и загруженное 
  вами в хранилище данных. Для достижения максимальной производительности у нас 
  есть личный кэш фрисайтов, которые вы посещали – клиентский кэш. Также у нас 
  есть «Slashdot кэш», который кэширует все остальное, но только за последние 30 
  минут, чтобы помочь с <a href="https://wiki.freenetproject.org/UltraLightweightPassiveRequests">распространением Frost/FMS сообщений</a> 
  и вспышками флуда. Мы планировали, что исправим ситуацию с помощью шифрованных 
  туннелей (премикс маршрутизация), но это сложный проект, который может дорого 
  обойтись для производительности. Изменения в билде 1226 могут немного снизить 
  производительность, но они позволят реализовать другие вещи, которые должны 
  заметно увеличить производительность, а также повышение полезности нод-узлов 
  Фринет во враждебных режимах. Ни одна система безопасности не совершенна, но 
  тем не менее Фринет может оказаться лучшим решением в конкретных ситуациях, 
  нежели другие альтернативы, особенно если вы можете использовать даркнет. 
  Фринет в режиме даркнет может работать там, где мало какое ПО еще сможет.</li>
  <li><b>Шифрование деликатных локальных данных</b><br>
  Новый клиентский кэш и база данных загрузок теперь могут быть зашифрованы. Это 
  можно настроить на странице «уровни защиты» в части про «физический уровень 
  безопасности». Если вы установите значение НИЗКИЙ, ничто не будет шифроваться 
  (даже временные файлы), Фринет будет использовать меньше ресурсов процессора и 
  работать немного быстрее. Если вы установите значение НОРМАЛЬНЫЙ или выше, 
  Фринет будет шифровать ваш клиентский кэш и базу загрузок. При уровне 
  безопасности НОРМАЛЬНЫЙ, ключи хранятся в файле master.keys, поэтому если вы 
  надежно удалите этот файл (или нажмете кнопку паники на странице загрузок), 
  доступ к данным вашего хранилища будет очень затруднен. При уровне безопасности 
  ВЫСОКИЙ, ключи все еще хранятся в файле master.keys, но теперь они защищены 
  паролем. На МАКСИМАЛЬНОМ уровне безопасности ключи не хранятся вовсе, каждый 
  раз при запуске Фринет создаются разные новые ключи; это означает, что ваша 
  очередь загрузки будет действовать лишь до перезапуска клиента Фринет, но вы 
  можете быть уверены, что после перезапуска нод-узла никто не сможет определить, 
  что вы с ним делали. Итак, у вас есть детальный контроль в компромиссе между 
  безопасностью, удобством и производительностью. Это позволяет обладателям 
  слабых компьютеров использовать НИЗКИЙ уровень безопасности; а жителям стран с 
  враждебными режимами, где высока вероятность изъятия компьютера, использовать 
  ВЫСОКИЙ ИЛИ МАКСИМАЛЬНЫЙ уровни безопасности.</li>
  <li><b>Подготовка файлового обмена c использованием фильтра Блума</b><br>
  Есть две причины. Первая – повысить безопасность людей, живущих во враждебных 
  режимах, когда их компьютер может быть изъят и у них могут оказаться проблемы 
  из-за того, что они делали в сети Фринет. Вторая – подготовить большую 
  производительность в версии 0.8 с использованием фильтра Блума для файлового 
  обмена. Идея заключается в том, что мы сообщаем нашим пирам какие ключи есть в 
  нашем хранилище данных, используя сильно сжатую структуру данных, называемую 
  фильтром Блума. С такой информацией мы прямо сейчас можем сказать, если 
  какая-нибудь из наших пир имеет искомый нами блок; это позволяет уменьшить 
  количество поисковых запросов и передач данных через многие другие нод-узлы. 
  Это должно значительно повысить производительность, уменьшить задержки, 
  увеличить скорость загрузки и повысить качество поиска редкого контента. 
  Следите за изменениями!</li>
  </ul>

