  <a name="xml-vuln"><h3>8 августа 2009 – <b>Срочно</b> обновите Java Virtual Machine, опасная уязвимость XML влияющая на Фринет</h3></a>
  <p>Если вы запускаете Фринет на Java ниже версии 1.6 билд 15 или версии 1.5 билд 20,
  пожалуйста обновитесь как можно скорее. Пока вы не обновите Java, в сети Фринет для 
