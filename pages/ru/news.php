<h2>WOT, FREETALK И ДРУГИЕ ПЛАГИНЫ</h2>
<p>Xor (также известный как p0s) продолжает работать над плагинами Сеть Доверия (Web of Trust) и Свободное Общение (Freetalk). Это приближает нас к дню, когда мы выложим их в доступ на странице плагинов, а затем включим их в состав дистрибутива по умолчанию.</p>
<p>Сеть Доверия – бэкэнд система, которая реализует сеть доверия псевдонимов, действующую подобным образом, как и в форумах Фринет (FMS). Вы можете создавать псевдо-личности (идентичности), управлять доверием к другим псевдо-личностям, заявить о своей псевдо-личности через CAPTCHA и так далее. Это карта сообщества, на которой вы можете видеть ваши идентичности и идентичности других людей, и доверительные отношение между ними. Сеть Доверия уже используют Freetalk, FlogHelper; вероятно, вскоре к ним присоединятся распределенный поиск, чат и другие.</p>
<p>Свободное Общение – устойчивая к спаму система общения, основанная на Сети Доверия. Она похожа на FMS, но в конечном итоге она будет включена в дистрибутив ПО Фринет и станет частью сети Фринет по умолчанию. Вы сможете разместить Freetalk блок на своем фрисайте. FlogHelper – плагин, основанный на Сети Доверия, для ведения флога (Фринет блога) легкий в использовании; использует Сеть Доверия для управления идентичностями. Я бы включил FlogHelper в комплект ПО Фринет еще несколько месяцев назад, но Сеть Доверия еще не готова, а FlogHelper нуждается в ней.</p>
<p>Вскоре Сеть Доверия будет готова. В последнее время была обнаружена важная проблема в алгоритме расчета доверия, также были исправлены и другие не такие значительные ошибки. Сеть Доверия станет полуофициальным плагином, который будет сначала работать в тестовом режиме. Грустно, что все старые сообщения и идентичности пропадут. Freetalk требует дополнительных усилий. Около 50% ошибок, выявленных в релизе 0.1 к настоящему времени исправлены.</p>
<p>Мы вставили новую версию библиотеки в билд 1240. Это великий шаг вперед, в сравнении с ее прошлой версией. Она быстрее, поддерживает внедрение поиска на фрисайте и имеет большое количество исправленных ошибок. Однако при поиске все еще могут происходить аварийные завершения работы.</p>
<p>Существует и другая проблема с библиотекой: infinity0 прошлым летом создал масштабируемый индексный формат для библиотеки, который сильно упростил бы создание и поддержание больших индексов. Скоро мы изменим Паука, чтобы он использовал новый формат индекса; также мы рассчитываем значительно увеличить производительность записи индексов. Думаю, что это не займет больше недели и уже выполняется. Сейчас я осуществляю то, что обещал сделать раньше. Это важно, поэтому это случится раньше или позже, лучше раньше.</p>
