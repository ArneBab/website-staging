<p>Очень интересно. В каждом случае мы вставляем проверочный файл размером 64КБ – один блок сверху и 4 под ним. Мы вставляем один блок три раза, а другие три блока вставляем по 1 разу каждый. Затем мы обращаемся к ним через неделю. Таким образом мы можем сравнить показатели успеха для одиночного блока вставленного лишь 1 раз, для одиночного блока вставленного три раза и для комбинированного MHK блока (блок, который был закодирован в 3 разных блока). Мы пытаемся получить все эти блоки; если хотя бы 1 любой блок сохранился успешно, мы можем восстановить другие.</p>
