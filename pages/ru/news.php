  <p>И наконец, принято долгосрочное решение построить darknet – сеть Фринет, где люди 
  устанавливают соединения непосредственно только с теми людьми, кому доверяют. Научите ваших 
  друзей использовать Фринет, и затем добавьте их в друзья на вашем нод-узле. Когда достаточное 
