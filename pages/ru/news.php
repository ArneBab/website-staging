<h2>СОХРАННОСТЬ ДАННЫХ</h2>
<p>У нас много идей, как улучшить сохранность данных. У сети довольно большая вместимость, но, кажется, данные становятся недоступными довольно быстро (статистика ниже). Я убежден, что улучшение сохранности данных улучшит юзабилити Фринет и положительно скажется на производительности. Популярность пункта «выкладывание по запросу» (insert on demand) в UserVoice наглядно демонстрирует это. Люди хотят, чтобы система работала. По моему скромному мнению мы можем улучшить положение вещей, не прибегая к «выкладыванию по запросу», хотя файловый обмен между клиентами основанный на распределенном поиске может в конечном итоге предложить его (но есть серьезные проблемы с безопасностью при «выкладывании по запросу»).</p>
<p>Эван убежден, что низкая сохранность данных не связана с какими-либо ошибками или сбоями в работе распределенного хранилища; скорее дело в малом количестве нод-узлов, которые хранят данные (не кэшированные копии), когда они переходят в автономный режим или становятся недоступными. Мы увеличили число нод-узлов хранящих данные; теперь нод-узлы используют свои хранилища для кэширования, если в хранилище есть свободное место; мы сделали много разных вещей, направленных на повышение сохранности данных, и еще больше мы можем сделать. Сразу возникает вопрос «не из-за улучшений ли безопасности связанных с кэшированием данных, достигнутых в прошлом году, появилась проблема с сохранностью данных» – мы проведем тест в билде 1241. Еще один связанный вопрос «почему вставка одного и того же ключа 3 раза дает такой огромный выигрыш в производительности по сравнению с ситуацией, когда вставка ключа осуществлена только один раз». Мы разберемся и с этим вопросом после. Мы, вероятно, включим тройную вставку верхних блоков после splitfiles в ближайшем будущем; но большей наградой будет достигнуть 90%+ успеха после недели использования тройной вставки одиночных блоков, и это вполне достижимо стоит лишь несколько изменить процедуру вставки…</p>
<p>И наконец, избыточность на клиентском уровне может быть намного умнее: мы разбиваем файлы на группы по 128 блоков (сегменты), а затем добавляем к каждому сегменту еще 128 проверочных блоков для обеспечения избыточности. К сожалению, это означает, что иногда последний сегмент имеет только один проверочный блок и поэтому он менее надежен, чем остальные сегменты. Мы исправим это.</p>
<p>Мы продолжаем собирать статистику по возможности найти и получить данные с течением времени. В худшем случае у данных есть сегмент имеющий только один проверочный блок. В реальной жизни после многих попыток (как минимум 2 проверочных блока требуется для прямого получения файла, если файл находится в очереди); у большинства сегментов избыточное количество проверочных блоков и для таких сегментов результаты должны быть существенно лучше. Каждый день мы вставляем 32 блока и получаем набор из 32 блоков: вчерашние 32 блока, позавчерашние 32 блока, 32 блока трехдневной давности, 32 блока вставленные неделю назад и т.д. Есть два таких процесса, чтобы получать больше данных, поэтому я привожу результаты для каждого из процессов. Процент показывает какая часть оригинальных вставленных блоков все еще доступна с течением времени:</p>
<table border="0">
<tr><td>1 день</td><td>76% / 77%</td></tr>
<tr><td>3 дня</td><td>66% / 70%</td></tr>
<tr><td>7 дней</td><td>60% / 61%</td></tr>
<tr><td>15 дней</td><td>48% / 48%</td></tr>
<tr><td>31 день</td><td>36% / 33%</td></tr>
<tr><td>63 дня</td><td>21% / 19%</td></tr>
