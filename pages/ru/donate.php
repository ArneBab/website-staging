      <h4>Почему проект Фринет нуждается в пожертвованиях?</h4>
      <p>Несмотря на бесчисленное количество людей, вложивших свое время и свои навыки в 
      проект с момента его создания, проект Фринет зависит от ваших пожертвований, как 
      для поддержки разработчиков, занятых на полный рабочий день этим все усложняющимся 
      проектом, так и для содержания аппаратной части (этот сервер) и административных 
      расходов (например, регистрация доменов). Мы также хотели бы создать фонд правовой 
      защиты в случае необходимости.</p>
      
      <p>Все вовлеченные в проект сильно заинтересованы в будущем проекта Фринет, и вы можете 
      быть уверены, дарения были и будут продолжаться, и будут использоваться с умом. Вся 
      поддержка ценится очень высоко.</p>
      
