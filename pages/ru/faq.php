  Fred (и Фринет в целом) все еще в активной разработке, и если что-то пойдет не так, стоит точно знать, что пошло не так.</p>
      
  <p><b id="stabchange">Как я могу переключаться между основной и тестовой сетями?</b><br>
  Тестовая сеть – самостоятельная сеть, которая позволяет разработчикам точно знать, что происходит. В тестовой сети нет 
  анонимности. Существует отдельный дистрибутив (для <a href="https://downloads.freenetproject.org/latest/testnet_installer_offline.jar">Linux, OS/X</a> 
  и <a href="https://downloads.freenetproject.org/latest/TestnetInstaller.exe">Windows</a>). Установленный клиент тестовой сети 
  может часто ломаться и вылетать, поэтому вам следует иметь некоторое представление, что вы делаете… или по крайней мере быть 
  готовым к регулярной переустановке клиента.</p>
      
  <p><b id="freenetinigone">Фринет не запускается, лаунчер говорит, что не удается найти freenet.ini</b><br>
  Это связано с неудачной ошибкой, исправленной в билде 1249. Вы можете исправить это, переименовав файл freenet.ini.tmp в 
  freenet.ini.</p>
      
  <p><b id="servicenotrespond">Фринет не запускается, говорит «служба не ответила на сигнал»</b><br>
  Это должно быть исправлено сейчас; дайте нам знать, если вы видите эту ошибку. Если у вас очень старая установка, вам может 
  быть интересно ознакомиться с <a href="https://wiki.freenetproject.org/Installing/Windows#Service_did_not_respond_to_signal">дополнительной 
  информацией</a>.</p>
      
  <p><b id="kaspersky">У меня установлен антивирус Касперского и Фринет не устанавливается или неизменное показывает «База данных очереди загрузок/выгрузок повреждена!».</b><br>
  У антивируса Касперского могут быть сложности с Фринет. Смотрите <a href="https://wiki.freenetproject.org/Installing/Windows#.27Download.2Fupload_queue_database_corrupted.21.27_.28When_using_Kaspersky_on_Windows_7.29">здесь</a>. 
  Мы рекомендуем вам отключать антивирус Касперского во время установки и во время запуска нод-узла, а также исключить из 
  области проверки директорию, куда вы установили клиентское ПО Фринет (скорее всего C:\Program Files\Freenet или 
  C:\Program Files (x86)\Freenet).</p>

  <p><b id="forgotpass">Я установил пароль и забыл его. Что я могу сделать?</b><br>
  Пароль защищает ваши загрузки/выгрузки, а также клиентский кэш (кэш всего, что вы недавно просматривали в сети Фринет). Он 
  хранится в файле master.keys. Нет никакого способа восстановить пароль, но если вы его все же забудете, вы можете уничтожить 
  ваши загрузки/выгрузки и клиентский кэш с помощью безопасного удаления файла master.keys. Смотрите <a href="#privatedata">вопрос 
  о личных данных и локальной безопасности</a> для дополнительной информации.</p>
  
  <p><b id="clockskew">Фринет постоянно жалуется на искаженное время</b><br>
  ПО Фринет будет испытывать проблемы, если вы будете постоянно перематывать ваше системное время. Обычно такое происходит, 
  когда что-то регулярно сбрасывает ваше время на большие значения. В Linux вам стоит запустить ntpd чтобы быть уверенным, что 
  ваши часы не слишком далеко (не жизненно необходимо, но полезно); если вы видите ошибку про искаженное время, попробуйте 
  добавить опцию «-x», чтобы избежать больших прыжков во времени назад. Кроме того, запуск ntpdate при загрузке системы 
  позволит сделать один большой скачок до запуска ПО Фринет. Это хорошая идея. Иногда такое может случиться и в Windows; 
  расскажите нам, как вам удалось это исправить… хотя в целом все не настолько серьезно, особенно если большой прыжок во 
  времени случается лишь раз в день.</p>

  <h2> Ответы на вопросы связанные с публикацией</h2>
  
  <p><b id="find">Если я опубликую что-нибудь в сети Фринет, как мою публикацию найдут другие пользователи сети? Не должны ли они знать ключ, который я использовал?</b><br>
  Да, люди должны знать ключ, который вы использовали для публикации вашей информации. Это означает, что вам придется объявить 
  о своем ключе.</p>
  
  <p>Наиболее распространенный способ сделать это – отправить сообщение, содержащее ваш ключ и краткое описание публикации, 
  автору одного из фрисайтов. Большинство порталов доступных в сети Фринет читают Freetalk и FMS форумы, там есть специальные 
  разделы для анонсов. Также вы можете отправить ваш ключ другим пользователям с помощью <a href="/lists.html">листа рассылки</a>, 
  на канале IRC (irc.freenode.net #freenet), личной почтой, или рекламируя свой фрисайт на обычном www-сайте. Если вы более 
  экстравагантны, даже можете попробовать скайрайтинг (skywriting). Граффити не рекомендуется из соображений легальности.</p>

  <p><b id="chk">Как я могу опубликовать хэш-ключ контента (CHK)?</b><br>
  Хэш-ключ для доступа к контенту формируется на основании содержания публикуемого контента; таким образом, ключ станет 
  известен только после загрузки контента в сеть. Чтобы вставить в сеть ключ, просто укажите его как «CHK@»; Фринет скажет 
  вам, что для актуального ключа вставка выполнена.</p>
  
  <p><b id="update">Возможно ли обновлять или удалять документы в сети Фринет?</b><br>
  В настоящее время документ, размещаемый в сеть Фринет, при совпадении имени с уже существующим документом в сети на самом 
  деле служит для распространения существующего документа. Также сейчас нет никаких средств удаления документа из Фринет. 
  Документы, которые никогда не запрашиваются, в конце концов удаляются из употребления.</p>
  
  <p>Тем не менее, вы можете использовать ключ <a href="https://wiki.freenetproject.org/USK">ключ USK (Updatable Subspace Key)</a> 
  для обеспечения обновления фрисайта: ваш нод-узел будет автоматически искать более поздние редакции сайта (после того, как 
  вы посетите сайт; или всегда, если вы сделаете для сайта закладку), и показывать вам последние версии. Вы можете принудить 
  нод-узел искать последнюю версию, изменив число в конце ключа на отрицательное.</p>

