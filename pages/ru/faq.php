  <h2> Ответы по безопасности</h2>
      
  <p><b id="browser">Могу ли я обозревать Фринет моим обычным браузером?</b><br>
  Фринет имеет веб-интерфейс: большая часть контента в сети Фринет – фрисайты и загрузки; настройками и подключениями с 
  друзьями можно управлять через веб-интерфейс. Однако, из-за слабостей современных браузеров, мы <b>настоятельно</b> 
  рекомендуем вам использовать отдельный браузер для Фринет. В частности, воровство истории браузера в любом виде является 
  серьезной угрозой; если вы будете использовать один и тот же браузер для сетей Фринет и WWW, вредоносные www-страницы 
  смогут определить, какие фрисайты вы посещали, и сообщить эту информацию своим владельцам.</p>
  
  <p>В последних версиях браузеров, обычно достаточно использовать безопасный/инкогнито режим, а приложение с кроликом в 
  системном трее в Windows попытается запустить браузер в этом режиме. Тем не менее, по нашему опыту это не на 100% 
  надежно, так что будьте осторожны.</p>
    
  <p><b id="attack">Не нарушит ли атака Х анонимность Фринет?</b></p>
  
  <p><b>Короткий ответ:</b> Наверное, для опеннет. Может быть, для даркнет.</p>
  
  <p><b>Длинный ответ:</b></p>
  
  <p>Фринет имеет различные модели угроз для Tor и Mixmaster отправителей. Фринет разработан устойчивым к цензуре: поэтому 
  должна сеть должна быть надежна, а контент должен распределяться без участия центрального севера для анонимности. 
  Анонимность важна для запрашивающих информацию, а еще больше для тех, кто выкладывает информацию в сеть. Типичным 
  примером являются корпоративные или государственные осведомители. Вообще, чтобы найти создателя какого-то контента, 
  злоумышленник должен быть способен предсказывать данные заранее, должен быть способен двигаться по сети довольно быстро, 
  и должен быть способен выполнить атаку в момент, когда контент еще только размещается в сеть; после этого контент 
  распределяется по сети и становится значительно сложнее выследить автора, который уже мог покинуть сеть. Однако, если по 
  воле случая или в случае действия непреодолимых сил злоумышленник подключился к осведомителю (или, как вариант, захватил 
  все компьютеры в сети), он может идентифицировать автора намного быстрее. Все это значительно сложнее выполнить для 
  сети, работающей в режиме даркнет, где каждый узел сети устанавливает подключения лишь с дружественными узлами, где 
  злоумышленнику очень сложно обнаруживать нод-узлы, и где для подключения к обнаруженному узлу злоумышленник должен 
  применить свои навыки социальной инженерии к оператору этого узла, чтобы стать его другом в сети. Фринет поддерживает 
  открытую сеть опеннет, но работа в режиме даркнет гораздо безопаснее и сложнее для блокирования национальными 
  брандмауэрами.</p>
  
  <p>С другой стороны Tor предназначен для обеспечения анонимности потоков данных в реальном времени, исходя из 
  предположения, что список узлов сети может быть публичным, что существует свободный мир, где узлы могут работать 
  безопасно; что авторы спорного контента могут либо скрыто держать веб-сервера сами, либо загрузить такой контент в другую 
  (скрытую, но обычно централизованную) систему хранения данных, и так далее. Tor имеет понятие «клиент» –пользователь 
  сервиса, который не предоставляет сети никаких ценностей; во Фринет каждый нод-узел транслирует данные для своих соседей. 
  Следовательно, атаки на Фринет полностью отличаются от атак на Tor. Обе системы в некоторой степени идут на компромиссы 
  для обеспечения производительности более-менее приближающейся к работе в реальном времени.</p>

  <p>Если вы можете использовать режим даркнет, доверяйте вашим друзьям, не вставляйте файлы повторно, всегда используйте 
  опцию «Вставить случайный, безопасный ключ», меняйте вашу анонимную идентичность после некоторого количество вставок. Это 
  должно обеспечить вам относительно безопасное использование Фринет. Однако, это все еще не доказано. Если вам требуется 
  подключиться, создать определенное доверие к вашей анонимной персоне, опубликовать ваш спорный контент и затем исчезнуть, 
  то вам подходит Фринет, особенно если ваш контент – это веб-сайт (но если вы подключаетесь в режиме опеннет, 
  остерегайтесь соседей). Для всего остального Tor лучше.</p>
    
  <p>Мы продолжаем работать над безопасностью Фринет, и существуют значительные усовершенствования, которые еще не были 
  реализованы; большая часть из них будет введена в ПО Фринет до версии 1.0. Криптографические туннели похожие на 
  луковичную маршрутизацию Tor – одна из возможностей, которая позволит значительно снизить влияние нижеперечисленных атак. 
  Также в планах несколько других усовершенствований, направленных на обеспечение анонимности пользователей, большей 
  надежности сети и устойчивости протокола к обнаружению.</p>
  
  <b>Основные известные атаки:</b><br>
  В интересах потенциальных пользователей мы предоставляем столько информации, сколько возможно. Также мы предполагаем, что 
  любой серьезный злоумышленник в любом случае делает свою домашнюю работу. Вот основные классы атак на Фринет, о которых 
  мы знаем к настоящему времени:</p>

  <ul><li><b>Сбор</b>: просто запустив несколько мощных нод-узлов Фринет, злоумышленник может относительно легко определить 
  большую часть нод-узлов опеннет (сеть Чужих). Эти обнаруженные нод-узлы могут быть атакованы по одному (при наличии 
  ресурсов), их трафик проанализирован или просто заблокирован национальным брандмауэром. Подключения только с Друзьями 
  (даркнет) в значительной степени решает эту проблему. Интернет-провайдеры в состоянии идентифицировать нод-узлы Фринет, 
  приложив для этого некоторые усилия, хотя мы делаем это достаточно сложным: текущий протокол Фринет разработан сложным 
  для обнаружения, в некоторый момент будет введена стеганография. Тем не менее, анализ транспортных потоков, или грубое 
  блокирование всего p2p-траффика (например, трафик между IP-адресами помечается как «потребительский» чаще, чем как 
  «деловой»), оба этих метода бьют и многих других, кроме Фринет; и, скорее всего, они будут эффективны еще в течение 
  достаточного долгого времени.</li>

  <li><b>Атаки при начальной загрузке</b>: если нод-узел устанавливает подключения не только с друзьями, он подключается к 
  опеннет нод-узлам сидерам (“seednodes”), чтобы заявить о себе и получить начальные пиры для подключения к ним. На данный 
  момент существует относительно мало нод-узлов сидеров, их список ведется вручную. Нод-узлы сидеры легко могут быть 
  заблокированы национальным брандмауэром и так далее. Есть небольшая защита по предотвращению возможных попыток 
  злоумышленников настроить свои собственные нод-узлы сидеры и встроить их в сеть, и затем «захватывать» новых 
  пользователей Фринет, которые подключаются к их нод-узлам сидерам, с целью наблюдать их трафик и так далее. Фринет 
  попытается объявлять несколько нод-узлов сидеров, но смотрите ниже пункт про корреляционные атаки, которые в основном 
  осуществимы только для единственного подключения к цели. Поэтому это вопрос ресурсов – если злоумышленник имеет ресурсы 
  для внимательного наблюдения за всеми новыми нод-узлами, у него есть хорошие шансы вытянуть нужный. В будущем мы можем 
  увеличить количество нод-узлов сидеров и будем раскрывать лишь малую их часть каждому подключающемуся нод-узлу, как 
  делает Tor с его скрытыми мостами, но это не помешает злоумышленникам создавать множество злонамеренных нод-узлов 
  сидеров и получать их в официальных списках; и, скорее всего, все еще возможно заблокировать все нод-узлы сидеры, 
  приложив некоторые усилия (что-то подобное уже случилось со скрытыми мостами Tor в Китае). Комбинация атак «сбор» и 
  «адаптивный поиск», эта атака объясняет почему сеть опеннет оценивается многими основными разработчиками как безнадежно 
  небезопасная. Если вы хотите хорошую безопасность, вам требуется подключаться только к друзьям. «Бейте и бегите» вставки 
  контента возможны и они относительно безопасны с точки зрения многих других атак, но вы принимаете риск, что нод-узел 
  сидер к которому вы подключаетесь может быть вредоносным.</li>

  <li><b>Атаки хранилища данных</b>: это в значительной степени решено в билде 1224, мы не кэшируем наши локальные запросы 
  и вставки, а также узлов подключенных к нам глубиной менее чем в 2 перехода (3 для вставок). Однако если ПО вашего 
  нод-узла имеет меньшую версию, захват хранилища данных может дать плохому парню некоторую интересную информацию. Также 
  обратите внимание, что клиентский кэш кэширует локальные запросы (но не вставки), поэтому он должен быть зашифрован и 
  защищен паролем. Это можно сделать, настроив уровень физической безопасности на ВЫСОКИЙ или ОТСУТСТВУЕТ. Вы должны также 
  зашифровать файл подкачки в частности и всю систему по возможности, чтобы предотвратить утечку информации через 
  веб-браузер, медиа-плееры и так далее. Заметим, что некоторые компрометирующие данные (например, список закладок) до сих 
  пор хранятся в незашифрованном виде; мы работаем над этим, но разве я еще не упоминал, что вам следует 
  <a href="http://www.truecrypt.org">зашифровать всю вашу систему</a>?</li>

  <li><b>Корреляционные атаки</b>: если вы подключены к нод-узлу и можете распознать запрошенные ключи (вероятно потому 
  что они были опубликованы), вы можете показать статистически, что узел, о котором идет речь, вероятно запрашивал их, 
  основываясь на доле ключей запрошенных с этого узла, расположение соседних узлов, HTL на запросы и так далее. Это будет 
  в значительной степени устранено туннелями (это довольно дорого обойдется для производительности, поэтому может 
  потребоваться отключить эту опцию по умолчанию, за исключением предсказуемых блоков), но в любом случае такая атака 
  требует более мощного злоумышленника в сравнении со следующей группой атак… Отметим также, что если вы подключаетесь 
  только к своим друзьям, то удаленный злоумышленник для атаки должен будет сначала скооперироваться с кем-то из ваших 
  друзей или социально взаимодействовать с вами, чтобы вы добавили его в друзья и предоставили возможность прямого 
  подключения; в любом случае, подключение ко всей сети таким способом достаточно дорого: если они уже подозревают лично 
  вас, вероятно, они скорее взломают вашу клавиатуру, чем будут пытаться подключиться к вашему нод-узлу Фринет!</li>

  <li><b>Адаптивный поиск</b>: если вы хотите найти автора некоторого контента, и вы способны предсказывать точные ключи, 
  которые будут вставлены, а также способны подключаться к новым нод-узлам по желанию, вы можете прослушивать трафик на 
  предмет ключей; угадывая откуда пришли ключи, вы подключаетесь к нод-узлам ближе к источнику, и если ваша догадка верна, 
  получите больше ключей, что даст вам больше точную информацию об авторе. Далее атака становится все быстрее и быстрее и 
  в конечном итоге определяет автора. Эта атака является самой мощной при вставке больших предсказуемых файлов, но опция 
  «Вставить случайный, безопасный ключ» сделает ключи непредсказуемыми даже для предсказуемого контента с помощью 
  случайных зашифрованных ключей. Недостаток этого способа заключается в том, что программное обеспечение создает 
  различные ключи каждый раз для того же самого файла, и вы никогда не сможете безопасно вставить повторно тот же самый 
  файл под тем же самым ключом. Учитывая, что сохранность данных в настоящее время относительно низкая, это проблема. В 
  любом случае, если вы <i>можете</i> использовать опцию случайного ключа, злоумышленник не способен продвигаться к вам, 
  пока вы не объявите о файле: большая часть совпадений с ключом будет поступать не от актуальной вставки контента, а в 
  сообщениях пользователей. Но есть и гораздо меньшая часть, с ней поможет периодическое изменение псевдонима идентичности 
  при условии, что злоумышленник не сможет легко связать новую и старую идентичности. Использование отдельной 
  идентичности, которая не общается слишком много, для публикации деликатного контента – это тоже поможет. Другая вещь, 
  которая имеет огромное значение – подключение только к своим друзьям (то есть использование режима даркнет): это делает 
  получение новых подключений к нод-узлам ближе к той части сети, где вы должны быть по его мнению, экстремально сложным 
  для злоумышленника; даркнет помогает, как и для корреляционных атак. Поэтому основные проблемы с этой атакой: 1) Файлы, 
  которые не очень популярны исчезают из сети Фринет довольно быстро, поэтому нуждаются в повторной вставке, но 
  небезопасно использовать один и тот же ключ для повторной вставки (вот для чего требуется опция «Вставить канонический 
  ключ»; для тех, кто не беспокоится об атаках), и 2) Чат все еще может быть атакован. Туннели помогут справиться с обеими 
  проблемами, и по умолчанию будут использоваться только для предсказуемых ключей, поэтому сеть может работать 
  относительно медленнее, но без риска столкнуться с этими проблемами. Также продолжается работа над разными техниками, 
  например, позволить пользователям совершать безопасную повторную вставку (обновление) контента, или предотвратить 
  возможность для злоумышленника просмотреть запросы, совершенные пользователем до его подключения. Другой важный момент – 
  это работает, только если источник загружает в сеть новый контент или общается в чате на регулярной основе. Создание и 
  загрузка новых псевдонимов идентичности через короткие периоды времени, использование одиночных первичных вставок 
  (любого размера) с включенной опцией безопасного случайного ключа и дальнейшее объявление ключа – это должно быть 
  относительно безопасно для таких атак даже в сети опеннет, но смотрите выше про атаки при начальной загрузке.</li>

  <li><b>Анализ трафика</b>: Фринет обеспечивает минимальную защиту от глобального анализа трафика (набор базовых 
  сообщений и так далее); если злоумышленник также имеет узлы в сети, дополнительные данные, вероятно, будут полезны. Мы, 
  конечно, не гарантируем что невозможно отследить передачу данных между нод-узлами с подробными данными о трафике, однако 
  есть надежда, что это уронит оживленный нод-узел. Однажды мы реализуем стеганографию транспорта данных и/или опцию 
  поддержания постоянной скорости подключений для самых параноидальных пользователей. Обратите внимание, что в Tor-сетях 
  глобальный анализ трафика победит сеть полностью: все что требуется – наблюдать обе точки входа и выхода.</li>

  <li><b>Атаки обмена</b>: можно атаковать алгоритм определения расположения, и, таким образом нарушить маршрутизацию в 
  дружественных (friend-to-friend) сетях. Это было продемонстрировано авторами документа Pitch Black. Мы работаем над 
  решением, но к сожалению, на данный момент большинство пользователей используют опеннет.</li></ul>
      
  <p>Более подробная информация о текущем состоянии безопасности Фринет доступна <a href="https://wiki.freenetproject.org/Security_summary">здесь</a>.</p>
      
  <p><b id="flooding">Уязвим ли Фринет к флуд-атакам?</b><br>
  Короткий ответ: нет.</p>

  <p>Длинный ответ:<br>
  Мы так не думаем. Помимо защиты свободы слова, Фринет разработан также, как система эффективного динамического 
  кэширования. Если информация много запрашивается с ограниченного количества нод-узлов, нод-узлы участвующие в обработке 
  запросов будут кэшировать передаваемую информацию, снижая сетевую нагрузку. Если информация вставляется на ограниченном 
  наборе нод-узлов, и затем впоследствии часто запрашивается другим набором нод-узлов, с повторением, наборы будут 
  сближаться в топологии сети до тех пор, пока не станут «соседями», и только изначально целевые нод-узлы страдают от 
  атак.</p>
  
  <p>Другими словами, в целях навредить Фринет с помощью флуда, вам необходимо последовательно изменять точку входа в 
  сеть и постоянно вставлять и запрашивать новые данные, и этим вы всего лишь увеличите нагрузку на сеть рядом с вами. 
  Обладая огромной волей и мощностью, большей чем мощность всей сети, можно вывести из строя любую сеть общего 
  пользования (в том числе и сеть Интернет) с помощью флуд-атак, но мы намерены всегда хранить Фринет устойчивым к таким 
  атакам, насколько это теоретически возможно.</p>
  
  <p>Любопытно, что приведенный анализ относится только к <a href="https://wiki.freenetproject.org/Opennet">опеннет</a>, 
  в даркнет вы могли бы иметь несколько больший успех, хотя вам будет гораздо сложнее менять вашу точку входа 
  существенным образом. Тем не менее, у вас есть достаточно низкая пропускная способность (общее количество посещенных 
  нод-узлов в среднем около 20), и вы значительно ограничены числом нод-узлов для подключения, оно будет ниже для 
  даркнет.</p>
      
  <p><b id="hash">Для чего нужны хэш-ключи и шифрование данных, если оператор нод-узла может идентифицировать их (данные) в любом случае, если он попытается?</b><br>
  Хэширование ключа и шифрование данных не предназначены для защиты операторов нод-узлов Фринет от возможности выяснить 
  какого типа информация хранится на нод-узле, если они действительно хотят (в конце концов, они могут просто найти ключ 
  таким же образом, как и кто-то запрашивающий информацию), а скорее, чтобы держать операторов нод-узлов в неведении о 
  информации, которая хранится на их нод-узлах, если они не хотят о ней знать. Это отличие больше правовое, чем 
  техническое. Нереально ждать от оператора нод-узла, что он попытается постоянно собирать и/или угадывать возможные 
  ключи, а затем станет сверять их с информацией на своем нод-узле (даже если такая атака жизнеспособна с точки зрения 
  безопасности), поэтому здравомыслящее общество с меньшей вероятностью возложит ответственность на оператора нод-узла 
  за такую информацию в сети.</p>

  <p><b id="cancer">Что насчет враждебных «раковых» нод-узлов в сети?</b><br>
  Существование враждебных узлов в сети является наиболее сложной проблемой, с которой может столкнуться распределенная 
  сеть, и было причиной гибели многих прежних идей. Многие системы (такие как многопользовательские игровые сети) 
  стараются избегать вредоносных узлов, сохраняя протокол и код закрытыми, но нам еще предстоит увидеть пример такой 
  работы на длинной дистанции. И в любом случае это противоречит философии Фринет.</p>
  
  <p>Фринет основан на балансе положительных и отрицательных петель обратной связей, которые приносят запросы 
  информации, с нод-узлом, когда он функционирует хорошо, и прячет запросы от него, когда это не так. Способ избежать 
  «рака» (как и с телом) – обеспечить, чтобы эти петли могли корректно определить даже самый тщательно разработанный 
  злонамеренный нод-узел и не продолжали посылать ему запросы. Этот вопрос еще не решен окончательно в текущем тестовом 
  коде, но вы можете быть уверены, что в течение некоторого времени были рассмотрены и обсуждены разные возможные 
  решения. Некоторые из них были реализованы (обеспечение хэшей или сигнатур для контента, таблицы ошибок для нод-узлов, 
  отступание от нод-узла для вызова таймаутов…)</p>

  <p><b id="attackY">Что насчет атаки Y?</b><br>
  Фринет все еще находится в тестировании и обязательно будут обнаружены атаки, с которыми мы еще не имели дела. 
  Поэтому, если вам удастся выявить действительно новый тип атаки, мы заинтересованы узнать об этом. Пожалуйста, 
  помните, чем Фринет является и чем не является. Ни одна сеть не может предложить все и всем, и есть вопросы 
  безопасности, которыми сеть Фринет, по своей природе, не занимается в той мере, какую вы, возможно, можете желать. 
  Если это расстраивает вас, весь наш код находится в свободном доступе, поэтому вы можете взять из него столько, 
  сколько хотите и написать собственную распределенную сеть, соответствующую вашим ожиданиям.</p>

  <p><b id="privatedata">Что личная информация делает в хранилище Фринет? Как мне избавиться от этого? Как я могу защитить мои компьютер и безопасность при работе в сети Фринет?</b><br>
  Прежде всего, мы <b>настоятельно</b> рекомендуем вам устанавливать ПО Фринет на зашифрованный жесткий диск, например, 
  с помощью <a href="http://www.truecrypt.org/">Truecrypt</a>. Невозможно в ПО Фринет предотвратить все утечки личных 
  данных, особенно если вы загружаете мультимедийные файлы и так далее. Даже если вы только смотрите фрисайты и 
  используете плагины для общения, все еще остаются компрометирующие данные в вашем файле подкачки, которые должен быть 
  зашифрован (в последних версиях Windows вы можете попробовать команду "fsutil behavior set encryptpagingfile 1", но 
  на самом деле решение – шифровать всю систему, включая файл подкачки). Также важно, чтобы вы использовали браузер в 
  режиме конфиденциальности или с отключенными в настройках историей просмотров и кэшированием; мы пытаемся сделать 
  это, когда вы запускаете клиент Фринет через значок-иконку с кроликом, но нет никаких гарантий; к сожалению, эта 
  функциональная возможность, кажется, глючит с текущими версиями веб-браузеров. Плагины браузеров тоже могут быть 
  проблемой, и вы должны использовать отдельный браузер для Фринет при любых подозрениях. Будьте осторожны с файлами, 
  которые вы скачиваете в сети Фринет – они могут не только позволить захватить ваш компьютер любому желающему для 
  просмотра (медиа-файлы могут быть записаны на диск, даже если вы открываете их непосредственно в вашем веб-браузере и 
  никогда не сохраняете их), но также они могут содержать угрозу анонимности, например такую, как выход на вредоносный 
  веб-сайт и так далее; такое возможно для pdf-файлов и некоторых видео-форматов.  Фринет пытается предупредить вас об 
  этом, когда он не может отфильтровать вредоносный контент: в настоящее время ПО Фринет фильтрует страницы HTML, 
  графические форматы GIF/PNG/JPEG, CSS и MP3. Вскоре мы добавим поддержку Ogg и других форматов позже. И конечно, есть 
  много других угроз – вы должны принять стандартные меры предосторожности: не работать в операционных системах, 
  которые не обновляются, не запускать программное обеспечение из ненадежного источника, использование соответствующее 
  ПО для безопасности и так далее (если у вас есть брандмауэр, убедитесь, что он пропускает два UDP-порта, используемых 
  Фринет).</p>

  <p>Поскольку не все пользователи будут иметь зашифрованный диск для установки Фринет в первый раз, Фринет сам 
  пытается зашифровать все потенциально компрометирующие данные, которые хранятся на диске. О деталях ниже, но как уже 
  объяснялось, утечки неизбежны: вы действительно должны <a href="http://www.truecrypt.org/">шифровать свои диски!</a></p>

  <p>Основное хранилище данных не хранит данные, которые вы запрашивали или вставляли (или которые запрашивались или 
  вставлялись соседними нод-узлами), потому что он может быть изучен другими нод-узлами. Это было введено, чтобы 
  исправить <a href="http://www.theregister.co.uk/2005/05/13/freener_not_so_anonymous/">атаку</a>, опубликованную 
  The Register. Фринет имеет отдельный клиентский кэш, который хранит данные, которые вы недавно запрашивали, чтобы 
  избежать лишних обращений к сети каждый раз (что не только снизит скорость, но также и безопасность, поскольку 
  предоставит злоумышленникам больше возможностей видеть ваши запросы). Кроме того, Фринет хранит список ваших загрузок 
  и выгрузок (к которому вы можете обратиться, выбрав в меню пункт «Обмен файлами»), их текущий прогресс, а также 
  разные другие данные, в файле node.db4o (или node.db4o.crypt). Сами данные хранятся в persistent-temp-* каталоге. 
  Если вы не установили уровень физической безопасности на НИЗКИЙ, эти данные шифруются. На МАКСИМАЛЬНОМ уровне ключи 
  шифрования никогда не записываются на диск, поэтому данные эффективно уничтожаются при перезапуске нод-узла; в 
  противном случае ключи шифрования хранятся в файле master.keys (на ВЫСОКОМ уровне используется пароль). Вы можете 
  уничтожить данные с помощью тревожной кнопки на странице загрузок/выгрузок или надежно удалив файл master.keys. 
  Фринет также создает временные файлы temp-* в каталоге temp для других запросов, которые также зашифрованы, если 
  физический уровень безопасности НИЗКИЙ. Кроме того, некоторые плагины могут создавать собственные файлы данных, 
  которые могут содержать сообщения с вашими ответами или загруженные сообщения из форумов и чатов; в настоящее время 
  закладки и информация о недавно загруженных файлах все еще хранятся в виде обычного текста. Наше намерение – 
  переместить их в node.db4o или хранить их в отдельных зашифрованных базах данных, как только появится 
  автоматическое резервное копирование для node.db4o. Смотрите <a href="https://wiki.freenetproject.org/Program_files">здесь</a> 
  детали по некоторым файлам.</p>
  
