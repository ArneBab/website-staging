  <h2> Философские ответы</h2>
  
  <p><b id="what">Что такое Фринет?</b><br>
  Фринет – бесплатное программное обеспечение, предназначенное для обеспечения подлинной свободы общения через Интернет.
  Оно позволяет любому желающему публиковать и запрашивать информацию, сохраняя полную анонимность. Никто не контролирует 
  Фринет, даже его создатели, а это означает, что система неуязвима для манипуляций или выключения. ПО Фринет очень 
  эффективно в работе с информацией, адаптивно реплицируя контент в зависимости от спроса. Для дополнительной информации 
  прочитайте страницу <a href="/whatis.html">«Что такое Фринет?»</a>.</p>
  
  <p><b id="tor">Чем Фринет отличается от Tor? Могу ли я получить доступ к Google/Facebook и другим ресурсам через Фринет?</b><br/>
  Tor – прокси-сеть, которая позволяет получить доступ к сети анонимно, или использовать «скрытые сервисы», веб-сервера 
  которых (и другие службы) скрыты за сетью туннелей Tor. Фринет – распределенная сеть, работающая поверх Интернет. Вы 
  можете получить доступ к контенту сети Фринет только через Фринет: фрисайты (веб-сайты в сети Фринет), чаты Фринет 
  (FMS, Sone и других), загруженные в сеть Фринет файлы, почта Фринет. Фринет является распределенным хранилищем данных, 
  что означает <i>полное отсутствие центральных серверов</i> (при правильном использовании) для хранения контента.
  Это делает цензуру и атаки гораздо сложнее. Чем популярнее файл или страница, тем шире она будет кэшироваться в сети и 
  тем быстрее будет загружаться. Недостатком является то, что некоторые вещи легкие при централизованной системе (интернет, 
  скрытые сервисы Tor), сложны для сети Фринет; например, динамический контент. Такие инструменты как форумы должны быть 
  специально написаны для Фринет таким образом, чтобы сохранить конфиденциальность и не полагаться на центральные сервера. 
  Как уже было написано, они анонимны и надежны против цензуры. Мы стараемся предоставить основные инструменты с помощью 
  плагинов, смотрите Гид по Социальной Сети Фринет, чтобы узнать как настроить некоторые из вещей описанных выше.</p>
  
  <p>Другая большая разница заключается в том, что Фринет может работать в режиме даркнет (другое название режима 
  Друг-с-Другом). В таком режиме работы ваш нод-узел будет устанавливать соединения только с нод-узлами ваших друзей 
  (и может быть к нод-узлам их друзей для ускорения процесса), которых вы добавили вручную. Это делает блокирование 
  Фринет, например, на национальном брандмауэре, крайне сложным.</p>
  
  <p>Строго говоря, есть несколько центральных серверов: большинство пользователей Фринет загрузило программное 
  обеспечение Фринет с этого сайта; большинство пользователей Фринет используют Фринет в режиме опеннет, то есть 
  подключение автоматически быстрее чем при работе в режиме даркнет – Фринет использует нод-узлы добровольцев 
  (seed nodes), как сервера для организации подключений пользователей к сети. Но это не является действительно 
  необходимым.</p> 
  
  <p>Таким образом, Фринет больше беспокоится о сопротивлении цензуре, чем о конфиденциальности и анонимности; 
  приватность имеет жизненно важное значение для предотвращения цензуры, но приватность не является достаточной без 
  надежной сети, которая может сохранить популярный контент, даже если автор контента исчезает. Поэтому Фринет 
  разработан сложным для блокирования, полностью отделенным от обычной веб-сети и децентрализованным.</p>

  <p><b id="who">Кто стоит за Фринет?</b><br>
  Фринет вырос из разработки для анонимных публикаций, созданной Яном Кларком во время учебы в Университете Эдинбурга в 
  Шотландии. С тех пор многие другие люди внесли свой вклад в воплощение предложения Яна в реальность.</p>

  <p><b id="trust">Как можно доверять информации анонимных авторов?</b><br>
  Цифровая подпись информации позволяет людям доказать авторство, этот метод часто используется для проверки подлинности 
  авторства электронных писем. Более того, вы можете подписывать информацию, не представляясь, тем самым становясь 
  анонимной персоной. Вы можете доказать, что вы написали различные части информации в сети Фринет, не раскрывая вашу 
  личность. Таким образом вы можете вырастить надежную репутацию вашей анонимной персоны.</p>

  <p><b id="donate-bw">Должен ли я предоставлять дисковое пространство и трафик?</b><br>
  В действительности вы ничем не жертвуете в том смысле, что вы теряете дисковое пространство и пропускную способность; 
  на самом деле вы ничего не распространяете что-либо (по меньшей мере, не таким же способом как делают другие программы). 
  Это больше похоже на загрузку в общий пул ресурсов сети Фринет.</p>

  <p><b id="leech">У меня нет ресурсов, которыми я мог бы поделиться с сетью и я хочу загружать больше.</b><br>
  Получаете ли вы контент анонимно? Фринет изначально разработан с идеей анонимности, производительность занимает второе 
  место.</p>

  <p><b id="donate-more-bw">Все мои друзья предоставляют сети очень мало дискового пространства и пропускной способности 
  своего канала. Зачем мне предоставлять больше ресурсов?</b><br>
  Если вас устраивает текущая скорость работы ПО, то ничего не требуется менять. Но если же вы хотите большего, вам стоит 
  рассмотреть вопрос о предоставлении большего количества ресурсов и организации работы вашего узла в режиме приближенному 
  к 24/7 насколько такое возможно. И попросите ваших друзей сделать то же самое.</p>

  <p><b id="donate-lot">Если я предоставлю много ресурсов, существенно ли улучшится мои впечатления от работы Фринет?</b><br>
  Ваши впечатления от работы Фринет, безусловно, станут лучше, но для действительно большого шага вперед нам нужно, чтобы 
  больше людей начали думать так же, как вы. Пропускная способность важнее дискового пространства.</p>

  <p><b id="legal">Легальность Фринет.</b><br>
  В настоящее время нам неизвестно ни одного судебного преследования непосредственно за использование Фринет. Некоторые 
  утверждают, что закон <a href="https://en.wikipedia.org/wiki/DADVSI">DADVSI</a>
  делает Фринет нелегальным во Франции; в Германии закон о хранении данных, требующий обязательную регистрацию, 
  <a href="http://en.wikipedia.org/wiki/Telecommunications_data_retention#Germany">отменен</a>.
  Кроме того, верховный суд Германии постановил, что <a href="http://merlin.obs.coe.int/iris/2010/7/article13.en.html">необеспечение 
  безопасности вашей WiFi-сети должным образом</a> делает вас ответственным за загрузки других людей через вашу сеть; это может 
  или не может быть распространено на запрещение анонимного пирингового файлового обмена такого как Фринет. <a href="http://en.wikipedia.org/wiki/ACTA">ACTA</a> 
  может иметь далеко идущие последствия, в том числе и для Фринет, и этот закон должен пройти, как и аналогичные законы вроде 
  IPRED2 которые были опробованы в прошлом. Также были попытки заставить пиринговые системы предоставлять возможности для 
  прослушивания переговоров в США; есть <a href="http://www.bbc.co.uk/news/uk-politics-19968068">тревожные события</a> в 
  Великобритании, в результате которых Фринет может быть заблокирован, но Фринет по сути не был признан нелегальным. Насколько 
  нам известно, ни одна из попыток, кроме первых двух, не увенчались успехом. Многие из них спорны так или иначе (в зависимости от 
  того, насколько широко применяется законодательство) и будут решены прецедентным правом. Иногда закон слеп. Вы можете прочитать 
  совет EFF (ориентированный на США) пиринговым разработчикам <a href="https://www.eff.org/wp/iaal-what-peer-peer-developers-need-know-about-copyright-law">здесь</a>.
  Если вам нужна юридическая консультация, поговорите с юристом. Также прочитайте следующий раздел, особенно если вы из Китая; 
  блокирование протокола может означать, что власть не любит нас.</p>

  <p><b id="blocked">Блокируется ли Фринет национальными брандмауэрами?</b><br>
  Китайский национальный брандмауэр (Золотой Щит) блокирует наш сайт в течение многих лет, а в 2005 году вдобавок заблокировал 
  протокол версии 0.5. Это означает, что в Китае нам не рады, поэтому будьте осторожны, если вы запустите Фринет в Китае. Некоторые 
  другие страны (например, Франция) враждебно настроены к пиринговому обмену файлами, и в конечном итоге могут заставить провайдеров 
  связи блокировать пиринговые сети (но прямо сейчас Фринет прекрасно работает во Франции и у нас много французских пользователей).</p>
  
  <p>Технически Фринет 0.7 имеет минимальную защиту от блокирования; протокол достаточно сложен в идентификации (мы работаем над 
  <a href="https://wiki.freenetproject.org/Transport_plugins">«транспортным плагином»</a>, который <a href="http://en.wikipedia.org/wiki/Steganography">сделает 
  обнаружение Фринет еще более сложным</a>. Фринет поддерживает режим работы <a href="https://wiki.freenetproject.org/Darknet">даркнет 
  (darknet)</a> (подключения устанавливаются только с друзьями), который делает сбор информации о нод-узлах сети Фринет и их 
  блокирование очень трудной задачей. Отметим, что многие беспроводные интернет-провайдеры блокируют все p2p сети вместе с другим 
  контентом, и многие корпоративные или учебные сети могут блокировать Фринет (но даже если они этого не делают, <a href="#trouble">вы 
  не должны запускать ПО Фринет на работе</a> в нерабочих целях!).</p>
  
  <p>Продолжаются дискуссии в США и Великобритании о требованиях законодательства встраивать бэкдоры везде и блокировать все, что не 
  имеет встроенных бэкдоров. Это вряд ли пройдет, особенно в США, где подобные законы предлагались периодически, но признавались 
  неконституционными. Тем не менее, даже если бы правительство пришло к нам и потребовало встроить бэкдор, мы не в состоянии сделать 
  это и тайно распространить троянскую сборку, потому что Фринет – это программное обеспечение с открытым исходным кодом, многие люди 
  вносят свой вклад в этот код, поэтому; таким образом юридически мы должны предоставить вам исходный код, включающий заказанные 
  правительством бэкдоры – которые не останутся тайной надолго! Если бы такое случилось, вероятно, что некоммерческая организация 
  <a href="/donate.html">Freenet Project Incorporated</a>, управляющая этим веб-сайтом и обработкой пожертвований, прекратила свою 
  деятельность и закрылась; но сама сеть Фринет будет жить хорошо и дальше лишь с той разницей, что проект не сможет так же 
  оплачивать труд разработчиков.</p>
  
  <p>Смотрите <a href="http://en.wikipedia.org/wiki/Network_neutrality">«сетевой нейтралитет»</a> и <a href="https://www.eff.org/">EFF</a> 
  или подобные организации в вашей стране, чтобы разобраться в политике всего этого и как вы можете остановить такие законы.</p>
      
  <p><b id="trouble">Могут ли у меня быть неприятности, если я поднял нод-узел?</b><br>
  Этот вопрос связан с вопросом <a href="#legal">«Легальность Фринет»</a>. Мы делаем все что можем, чтобы сделать чрезвычайно 
  сложным для любой разумной правовой системы наказание кого-либо за запуск нод-узла; существует мало прецедентов таких действий в 
  развитых странах. Многие правовые системы признают важность свободы слова, которая является основной целью Фринет. Скажу, что
  есть риск в любой вашей деятельности, с которой может не согласиться ваше правительство; вы должны принять обдуманное решение, 
  прежде чем пойдете на такой риск. Кроме того, ваш провайдер или ваш хостинг могут иметь проблемы с Фринет. Как минимум один 
  французский хостинг заблокировал Фринет (вместе с TOR и другими) на своих серверах; пожалуйста, прочитайте ваши правила и условия, 
  чтобы убедиться, что вам позволено запускать ПО Фринет. Отметим также, что Фринет может использовать довольно много трафика, как 
  минимум 20ГБ/месяц, и это может быть проблемой на дешевом или общем подключении. И, конечно, использование ПО Фринет на работе 
  может вовлечь вас в неприятности тоже, если вы не используете его в рабочих целях.</p>

  <p><b id="copyright">Об авторском праве.</b><br>
  Есть несколько отличных мыслей на эту тему на странице <a href="/philosophy.html">«Философия»</a>. С некоторыми законами по 
  авторскому праву у Фринет могут быть проблемы. Пожалуйста, прочитайте <a href="#legal">«Легальность Фринет»</a> и 
  <a href="#blocked">«Блокируется ли Фринет национальными брандмауэрами?»</a></p>

  <p><b id="childporn">Как насчет детского порно, материалов оскорбительного содержания или терроризма?</b><br>
  В то время как большинство людей желает, чтобы детской порнографии и терроризма не существовало, человечество не должно лишать 
  себя свободы общения только лишь из-за того, как очень небольшое количество людей сможет использовать такую свободу.</p>

  <p><b id="offensive">Я не хочу, чтобы мой нод-узел использовался для «скрытого» хранения детского порно, материалов 
  оскорбительного содержания или чего-нибудь связанного с терроризмом. Что я могу сделать?</b><br>
  Настоящая проверка утверждающих, что они верят в свободу слова – то, насколько они терпимы к речам, с которыми не согласны или 
  даже находят отвратительными. Если такое для вас не приемлемо, вам не стоит поднимать нод-узел Фринет. Кроме того, контент в сети 
  Фринет доступен лишь до тех пор пока популярен; он исчезнет, когда пользователи потеряют интерес к нему. Тем не менее, контент 
  должен храниться в течение некоторого времени, и если достаточное количество людей будет им интересоваться, он будет храниться 
  вечно.</p>

  <p><b id="export">Что насчет экспортных ограничений шифрования?</b><br>
  Проект Фринет уведомил власти США (с размещения файлов на ресурсе SourceForge, который расположен на территории США), что в 
  рамках проекта будет экспорт шифрования. Пока ваша страна не запрещает использовать шифрование, у вас все отлично. Кроме того, в 
  настоящее время в экспортных законах существует исключение для программного обеспечения, которое делает именно то, что делает 
  Фринет. Тем не менее, Sun ограничивает силу шифрования доступного в виртуальной машине Java, на которой работает Фринет; вы 
  должны установить Unlimited Strength Policy Files для Java для повышения производительности, если такое возможно. Однако, Фринет 
  будет работать и без этого, используя встроенные функции шифрования.</p>

  <p><b id="whatelse">Мне нечего скрывать и я не нуждаюсь в анонимности. Есть что-нибудь еще, что Фринет может мне предложить?</b><br>
  Даже без своего основного достоинства - анонимности, Фринет очень полезен из-за своего уникального способа распределения контента 
  и информационной нагрузки. Проще говоря это означает, что вы можете опубликовать веб-сайт, не беспокоясь о том каким большим 
  будет сайт и что кто-то разместит рекламные баннеры на нем. Хотя маловероятно, что фрисайты будут загружаться быстрее обычных 
  веб-сайтов, они (фрисайты) лучше приспособлены к внезапному всплеску посетительской активности (что часто случается, когда 
  относительно неизвестный сайт получает ссылку с большого портала), и разумная скорость загрузки больших файлов возможна тоже. 
  Только не ожидайте очень низкую латентность сети.</p>
      
  <h2> Технические ответы</h2>

  <p><b id="how">Как я могу использовать программное обеспечение? Я загрузил его и запустил, но не вижу графического интерфейса.</b><br>
  Фред (Fred от Freenet Reference Daemon) работает как демон или служба в фоновом режиме. Обычно вы взаимодействуете с ним через 
  клиент Фринет. Встроенный клиент fproxy позволяет вам общаться в сети Фринет с помощью веб-браузера. При установке ПО Фринет 
  создается ярлык запуска «Browse Freenet» на рабочем столе и/или в меню «Пуск». Другой способ – иконка в трее (кролик), пункт 
  меню «Open Freenet». Если предложенное выше не работает, попробуйте открыть в браузере страницу шлюза <a href="http://127.0.0.1:8888/" rel="nofollow">http://127.0.0.1:8888/</a>. 
  Пройдитесь по предложенным ссылкам, чтобы добраться до нескольких популярных сайтов-каталогов Фринет.</p>  
  
  <p>Если вы ищете приложения, которые работают поверх Фринет и предоставляют разные интерфейсы или функциональные возможности, пожалуйста, посмотрите страницу <a href="/tools.html">«Инструменты»</a>.</p>
      
  <p><b id="slow">Почему Фринет такой медленный?</b><br>
  Когда вы только установили ПО Фринет, оно будет работать медленно и вы можете увидеть ошибки «данные не найдены» или «не 
  найден маршрут» при открытии фрисайтов. Это нормально, Фринет значительно ускорится с течением времени. Для лучшей 
  производительности вы должны стремиться, чтобы ваш клиент Фринет работал 24 часа в сутки. Именно поэтому мы устанавливаем 
  Фринет службой.</p>
  
  <p>Пожалуйста, имейте ввиду, что Фринет по своей природе сеть с высокой латентностью: может потребоваться некоторые время 
  для загрузки страницы в первый раз, но все же это разумная скорость загрузки (по меркам анонимных систем!). Вы увеличите 
  производительность при просмотре фрисайтов, если будете использовать отдельный браузер и <a href="#connections">увеличите 
  его ограничения подключений</a>. Также Вам стоит установить размер хранилища данных и ограничение пропускной способности 
  такими высокими, насколько возможно. Защита вашей анонимности стоит некоторое количество производительности. Вы можете 
  определить сколько, меняя уровни безопасности на странице «Настройки» – «Уровни защиты».</p>

  <p><b id="search">Работает ли поиск в сети Фринет?</b><br>
  Да, существует несколько различных механизмов поиска. Для поиска по фрисайтам (веб-сайтам в сети Фринет) вы можете 
  воспользоваться формой поиска на домашней странице или выберите в меню пункт «Обзор» -- «Поиск во Фринет». Если его там нет, 
  пройдите на страницу «Настройки» - «Дополнения» и загрузите библиотеку плагина. Кроме того, Frost и Thaw обеспечивают поиск 
  сообщений и файлов. Обратите внимание, что поиск в сети Фринет намного сложнее, чем в других сетях; в связи с особенностями 
  архитектуры сети Фринет и поставленными перед ней задачами.</p>

  <p><b id="firewall">Как настроить Фринет для работы за брандмауэром/NAT?</b><br>
  В целом, Фринет должен работать с NAT. Тем не менее, вам стоит вручную перенаправить порты, если такое возможно. Откройте 
  страницу <a href="http://127.0.0.1:8888/friends/" rel="nofollow">«Друзья»</a> или страницу <a href="http://127.0.0.1:8888/strangers/">«Чужие»</a>.
  Внизу вы увидите список портов, используемых нод-узлом. Вы должны перенаправить (для UDP) даркнет FNP и опеннет FNP порты. 
  Возможно, вам придется изучить документацию к маршрутизатору, чтобы выяснить как это сделать. Фринет должен перенаправлять 
  их себе с помощью <a href="https://en.wikipedia.org/wiki/Universal_Plug_and_Play">UPnP (Universal Plug and Play)</a>, но 
  такое не всегда работает (и такое точно никогда не сработает, если у вас не установлен UPnP плагин; или если за вашим 
  маршрутизатором есть другой маршрутизатор).</p>
  
  <p>Если у вас есть адрес DynDNS или другое доменное имя, указывающие на компьютер, с которого вы запускаете нод-узел Фринет, 
  сообщите нод-узлу об этом. Перейдите на страницу <a href="http://127.0.0.1:8888/config/node?mode=2">«Настройки» -- «Основные настройки»</a> 
  (в расширенном режиме) и найдите пункт «Задать IP-адрес вручную». Введите ваше доменное имя в это поле и примените настройку.</p>

  <p><b id="connection-perm">Требуется ли постоянное подключение к сети для запуска нод узла?</b><br>
  Нет, но такой вариант предпочтителен. Вы можете запустить ПО Фринет и протестировать его работу через «временные» 
  подключения (например, телефонный дозвон или мобильный модем), но для сети в целом для большей ее полезности, мы нуждаемся 
  в как можно большем количестве постоянно работающих нод-узлов (большая часть подключений посредством кабельных и DSL 
  модемов достаточно «постоянна» для этого). Более поздняя версия Фринета сможет лучше использовать нод-узлы с непостоянным 
  доступом к сети.</p>

  <p><b id="connections">Почему клиент Фринет загружает только 1 или 2 файла одновременно?</b><br>
  Многие браузеры ограничивают число одновременных подключений к чему-то слишком низким значением для эффективного серфинга в 
  сети Фринет (страницы в сети Фринет при загрузке часто имеют гораздо более высокие задержку, чем обычные веб-страницы). 
  Обычно эту настройку можно изменить. Например, в Mozilla Firefox введите <span style="font-weight: bold;">about:config</span> 
  в адресной строке браузера и измените значения следующих настроек на приведенные ниже. Используйте фильтр <span style="font-weight: bold;">&quot;connections&quot;</span>, 
  чтобы отобрать только нужные настройки:</p>

  <code>
  network.http.max-connections 200<br>
  network.http.max-connections-per-server 200<br>
  network.http.max-persistent-connections-per-proxy 200<br>
  network.http.max-persistent-connections-per-server 200<br>
  </code>
  
  <p>Заметьте, что при таком изменении настроек Mozilla будет использовать большее количество подключений для всего, что вы 
  смотрите; и это может оказаться нежелательным с точки зрения перегрузки сети. Но в идеале вы должны использовать отдельный 
  браузер для Фринет для лучшей безопасности.</p>

  <p><b id="store-perm">Почему Фринет не может хранить данные постоянно?</b><br>
  Потому что мы не можем найти способ реализации без ущерба другим целям и задачам Фринет. Например, люди часто предлагают, 
  чтобы нод узел никогда не удалял данные, которые они хотят кэшировать постоянно. Однако, это не будет работать, даже если 
  данные все еще будут доступны на их нод-узле, нет никакого способа обеспечить маршрутизацию запросов данных до этого 
  нод-узла. Мы рассмотрели много разных способов как Фринет сможет хранить данные перманентно, но ни один из них не 
  работает, либо является компромиссным решением в ущерб основным целям – анонимности и масштабируемости.</p>
  
  <p>Контент, который пользуется популярностью, будет сохраняться всегда. Например, большинство фрисайтов на которые есть 
  ссылки в основных индексах все еще доступны спустя годы (по меньшей мере их главные страницы). Если контент не очень 
  популярен, то лучший способ сохранять его доступность – регулярно перезагружать его в сеть. Интересным решением является 
  плагин «Keepalive», который сделает это за вас, даже если вы не загружали файл/фрисайт. Планируется улучшение – особый вид 
  запроса, который позволит проверять доступность файла из случайной точки сети.</p>

  <p><b id="why-java">Почему Фринет реализуется на Java?</b><br>
  Существуют различные мнения по выбору Java для реализации Фринет (даже среди основных разработчиков). <a href="/people.html">Ян Кларк</a> 
  и несколько других разработчиков сторонники Java и сделали выбор в пользу Java. Даже если все сойдутся во мнении 
  переключиться на другой язык, повторная реализация текущего протокола Фринет будет довольно большой задачей и займет 
  значительное количество времени, потому что нам доступно ограниченное количество времени разработчиков. Флейм в листе 
  разработки на тему выбора другого языка не приветствуется. Кто хочет реализовать Фринет на другом языке, можете 
  попытаться. Однако, не стоит недооценивать объем работы.</p>
      
  <p><b id="fproxy-lan">Как разрешить подключения к FProxy с других компьютеров?</b><br>
  Если вы хотите, чтобы все могли использовать ваш нод-узел, у вас есть следующие варианты:<br></p>
  
  <ul>
  <li>Откройте страницу <a href="http://127.0.0.1:8888/config/fproxy">«Настройки» -- «Веб-интерфейс»</a> и включите расширенный режим.</li>
  <li>Остановите работу вашего нод-узла и отредактируйте файл freenet.ini вручную.</li>
  </ul>
  
  <p>В обоих случаях измените следующие параметры:</p>
  
  <code>
  fproxy.bindTo=0.0.0.0<br>
  fproxy.allowedHosts=*<br>
  </code>
  
  <p>Конечно, это оставляет ваш нод-узел широко открытым в отсутствие брандмауэра. Если вы предпочитаете использовать контроль доступа 
  к Фринет, то вы можете использовать такие строки:</p>
  
  <code>
  fproxy.bindTo=0.0.0.0<br>
  fproxy.allowedHosts=127.0.0.1,192.168.1.0/24<br>
  </code>
  
  <p>Или даже (найдите ваш IP-адрес в ipconfig/ifconfig/winipcfg и замените его на 192.168.1.1):</p>
  
  <code>
  fproxy.bindTo=127.0.0.1,192.168.1.1<br>
  fproxy.allowedHosts=127.0.0.1,192.168.1.0/24<br>
  </code>
  
  <p>И если вы хотите предоставить полный доступ (то есть изменение настройки конфигурации, перезапуск и т.д.) к нод-узлу (ВНИМАНИЕ: 
  Будьте очень осторожны предоставляя кому-нибудь полный доступ к FProxy!):</p>
  
  <code>
  fproxy.allowedHostsFullAccess=127.0.0.1,192.168.1.0/24<br>
  </code>

  <p><b id="openjdk">Проблемы с OpenJDK при установке</b><br>
  <p>Некоторые версии OpenJDK, а особенно которая включена в Ubuntu 8.04, имеют некоторые проблемы с SSL; из-за них происходит 
  ошибка при установке. Пожалуйста, установите Sun JRE, как минимум 6-й версии. В Ubuntu или Debian, откройте терминал с правами 
  пользователя root и выполните:</p>
  
  <pre>
  apt-get install sun-java6-bin
  update-java-alternatives -s java-6-sun
  </pre>

