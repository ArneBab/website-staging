<h1>Applications

</h1>
<br />A variety of people have written some excellent tools and clients for use with Freenet.
<br /><p>
<h3><a href="http://www.freenetproject.org/index.php?page=fphowto">Development Aides</a></h3>
<p>
<br /><h3>Freenet Publishing & Retrieval Tools</h3>

<br /><a class='wiki'  href='http://127.0.0.1:8888/SSK@M7yZgrl8gwtAe1xEcR5Xyv4tFsoPAgM/fiw/9//'><b>Freesite Insertion Wizard</b></a> <b><span style='color:#FF0000;'>You must be running Freenet for this link to work.</span></b>
<br />
<br />FIW is a feature packed Java AWT application for inserting freesites.  It contains a lot of help at each stage of the process, and includes site verification to increase availability of the inserts.
<br />
<br /><a class='wiki'  href='http://127.0.0.1:8888/SSK@kWu5Osv~VAI3-kH7z8QIVxklv-YPAgM/fishtools/52//'><b>FishTools</b></a> <b><span style='color:#FF0000;'>You must be running Freenet for this link to work.</span></b>
<p>
<br /><a class='wiki'  href='http://127.0.0.1:8888/SSK@CKesZYUJWn2GMvoif1R4SDbujIgPAgM/fuqid/12//'><b>Freenet Utility for Queued Inserts and Downloads</b></a> <b><span style='color:#FF0000;'>You must be running Freenet for this link to work.</span></b>
<br />
<br />FUQID is a Windows program (but it will run in Wine under Linux) for inserting and downloading large files. It offers fast native CHK calculation and FEC decoding and a simple, highly configurable interface, better than FProxy.
<br />
<br />Freesite insertion tools written in Python (standalone binaries are available for Windows). Includes FEC support for alternative handling of large files.
<br />
<br /><a href='index.php?page=fcptools' class='wiki'><b>FCPTools</b></a>
<br />
<br />The FCPTools are command-line driven programs for inserting and retrieving files with Freenet. Included separately is <a href='index.php?page=FCPlib'><b>FCPLib</b></a>, the Freenet Client Protocol Library. The FCPTools are linked against FCPLib and serve as nice examples for using the library in Freenet client programs. Everything is released under the GNU General Public License.
<br />
<br /><hr><h3>Message Board Tools and File Sharing tools
</h3>
<br /><a class='wiki'  href='http://jtcfrost.sf.net/'><b>Frost</b></a>
<br />
<br />Frost is a file-sharing tool for Freenet, it also has chat forums which allow people to chat anonymously over Freenet. It now supports FEC (which makes downloads more reliable).
<br />
<br /><a class='wiki'  href='http://127.0.0.1:8888/SSK@h~ixmz11-tDOox9O1gQyjkzAUCcPAgM/fmb/5//'><b>Freenet Message Board</b></a> <b><span style='color:#FF0000;'>You must be running Freenet for this link to work.</span></b>
<br />
<br />FMB is an excellent Usenet-like tool which allows secure messages to be exchanged via Freenet, it is somewhat similar to Frost, but employs a more sophisticated "outbox" based approach. 
<br />
<br />It seems that the original author does not have the time to release new versions of it, some other Freenet users created modified versions. For more information visit <a class='wiki'  href='http://127.0.0.1:8888/SSK@rjYFfgPHfolmcStiaoxESFfBXz8PAgM/FMBwishlist/11//'>FMB Wishlist</a>. <b><span style='color:#FF0000;'>You must be running Freenet for this link to work.</span></b>

<br />
<br /><a class='wiki'  href='http://www.storrow.org/Software/FreeNews/'><b>FreeNews</b></a>
<br />
<br />FreeNews is an NNTP server that uses Freenet as its storage medium.  It allows you to use your favorite newsreader to read Text Over Freenet messages, like frost uses.
<br />
<br /><hr><h3>Misc Tools
</h3>

<br /><a class='wiki'  href='http://127.0.0.1:8888/SSK@UjHShzjg6iS5UNGGb4IpFPhHGeoPAgM,p7qut8fPyia~CgO7noQdLA/pm4pigs/10//'><b>pm4pigs</b></a> <b><span style='color:#FF0000;'>You must be running Freenet for this link to work.</span></b>
<br />
<br />
pm4pigs is a freenet client written in python for sending and receiving encrypted messages.
<br />

<br /><a class='wiki'  href='http://sf.net/projects/liberator'><b>Liberator</b></a>
<br />
<br />Liberator is a Unix command-line client designed to be easy to script (or cron). It's written in perl so it should be as portable as needed.
<br />
<br /><b>Update:</b> new versions written in OCaml will be released in binary form for Win32, Linux/x86, Linux/PPC, and OSX.
<br />

<br /><a class='wiki'  href='http://127.0.0.1:8888/SSK@fgbuxwSCCjOJHsUI-9-uijD1haQPAgM/flinks/11//'><b>Flinks</b></a> <b><span style='color:#FF0000;'>You must be running Freenet for this link to work.</span></b>
<br />
<br />Flinks is an application to help people gather, manage, and share links in freenet.  Currently, several indicies generate flinks data (<a class='wiki'  href='http://127.0.0.1:8888/SSK@-w495UL3mfSlWC2c~nRAuG2fAWwPAgM/TFEE//'><b>The Freedom Engine Engine</b></a>, <a class='wiki'  href='http://127.0.0.1:8888/SSK@Sc6qV~D6iFhaYord6HtbjJ8MaEYPAgM/YoYo//'><b>YoYo</b></a>, and the <a class='wiki'  href='http://127.0.0.1:8888/SSK@rjYFfgPHfolmcStiaoxESFfBXz8PAgM/FreenetHelp//'><b>Freenet Help Index</b></a>, among others (<b><span style='color:#FF0000;'>You must be running Freenet for those links to work.</span></b>)), so you can go to their page and download their links, allowing you to essentially build your own index with sites you like.<p>

