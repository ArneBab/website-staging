<h3>News</h3>
<b>6th August, 2004 : Stable build 5090, Stable network reset</b>
<p>Stable build 5090 has now been released, all users running the stable
branch should upgrade as this build contains numerous important bugfixes,
including a fix for a 4-month-old bug that was causing lots more Route
Not Found errors than there should be. Also, the stable network has been
reset as of 5090, so you will need to reseed. This is partly because of
the big changes in 5085, and partly because of bugs in 5085 up to 5088.
Instructions for installing or upgrading are <a href="/index.php?page=download">
here</a>. A more detailed list of changes may be found on 
<a href="http://article.gmane.org/gmane.network.freenet.support/5090">
the announcement</a>, or for the real Freenet geeks out there, the low 
level changelog is on the 
<a href="http://news.gmane.org/gmane.network.freenet.cvs">CVS list archive</a>.
If you have any problems upgrading don't hesitate to contact us on IRC on 
#freenet on irc.freenode.net, or by email at 
<a href="support@freenetproject.org">support@freenetproject.org</a>.</p>
<b>23rd July, 2004 : Stable build 5085</b><br>
<p>
Stable build 5085 has now been released, all users running the stable
branch should upgrade as this build contains important bugfixes and
many fundamental improvements.  Instructions for installing or upgrading
are available <a href="/index.php?page=download">
here</a>.  A more detailed list of changes may be found 
<a href="/index.php?page=whatsnew">here</a>.
</p>
<b>12th July, 2004 : Status Update</b><br>
<p>
Things have been progressing rapidly in the unstable branch over the
past few weeks.  Along with the usual flow of bug fixes, Matthew
has made some interesting discoveries by setting up a local "testnet",
and is in the process of integrating these lessons into the 
<a href="/index.php?page=ngrouting">next-gen routing</a> algorithm.
We have continued to experience problems with excessive "route not
found" errors, particularly when a node has just started up, and
are working on a variety of solutions to this problem.
</p>
If you are feeling adventurous, <a href="?page=tryunstable">this page</a>
explains how you can try out the unstable branch of development.
