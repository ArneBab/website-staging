<h3>News</h3>
<p><b>23rd August, 2003 - Next Gen routing now in experimental branch</b></p>
The most fundamental improvement to Freenet's core algorithm, "Next Generation
Routing", is now in the experimental branch of CVS.  NGrouting holds the
promise of making Freenet much faster than it is today, while improving
virtually all aspects of Freenet's performance.  To learn more about
NGrouting see <a href="/index.php?page=ngrouting">here</a>, if you would
like to try it out see <a href="/index.php?page=tryng">here</a>.

<p><b>21st July, 2003 - Next Generation Routing Paper</b></p>
Ian just finished a paper describing "Next Generation Routing", a pretty revolutionary
change to the core of Freenet's operation that has the potential to make Freenet as
fast, and in some cases even faster, than the World Wide Web.  Read it 
<a href="/index.php?page=ngrouting">here</a>.

