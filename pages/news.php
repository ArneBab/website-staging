<h3>News</h3>
<p><b>16th July, 2003 - Freenet 0.5.2 Released!</b><br></p>
<p>After four months work, Freenet 0.5.2 is now <a href="/index.php?page=download"><b>available
for download</b></a>.</p> <p>It features a new NIO technology that brings improved performance 
using less CPU and system resources. Individual
nodes are now more efficient and the speed and routing of the entire network have significantly 
improved.</p><p> We also implemented
probabilistic caching, which further improves routing and lets Freenet
store and find more data. There are some user interface
improvements, more improvements to downloads of large files,
a fix for the Heisenbug and tons of others.</p><p>Progress has only been 
possible at this rate because, thanks to your donations, the project has been able to pay a
full time developer for the bargain rate of $1500/month.  We have some very exciting things 
planned for the next release such as a revolutionary new routing mechanism called NGRouting, so if you haven't already done so, please 
consider <a href="/index.php?page=donate">donating</a>.</p>

