<h3>News</h3> 
<p><b>15th October, 2003 - New stable build: 5034</b></p>
A new stable build of Freenet is now available.  Among the core 
improvements are a rethink of load balancing, and some 
measures to help nodes route information more effectively from the 
moment they start.  This should result in a better experience for new 
users, along with superior overall performance.
<p>
As usual with Freenet, the full benefit of these improvements will only 
really become apparent when the new version of Freenet is widely 
deployed.  Windows users can update by running the "Update Snapshot" 
program in the Freenet section of their start menu, and Linux users can 
update by running the <b>update.sh</b> script in their Freenet 
directory.
