<h2>Successful installation of Freenet 0.7!</h2>

Congratulations, you have successfully installed a Freenet 0.7 alpha-test node.<br />
<br />
You can access it <a href="http://127.0.0.1:8888/">here</a>.<br />
<br />
If you installed a testnet node you can access it <a href="http://127.0.0.1:8889/">here</a>.<br />
<br />
These links will take you to FProxy, the web interface to Freenet. You can also run Frost, a message boards program, which may have been installed; there may be an icon on your desktop, otherwise have a look at the directory the node was installed to.<br />
<br />
You will need to get some darknet (or testnet) connections. If you know people using Freenet, ask them for their noderefs (this is ideal), otherwise please join us on IRC on #freenet on irc.freenode.net, and we will get you wired in to the network.<br />
<br />
Once again thanks for using Freenet!<br />
