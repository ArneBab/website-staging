      <h1>Qu'est-ce que Freenet ?</h1>
      <blockquote>
	<i>"I worry about my child and the Internet all the time, even though
	  she's too young to have logged on yet. Here's what I worry about. I
	  worry that 10 or 15 years from now, she will come to me and say 'Daddy,
	  where were you when they took freedom of the press away from the
	  Internet?'"
	</i><br/>
	--Mike Godwin, <a href="https://www.eff.org/">Electronic Frontier
	  Foundation</a>
      </blockquote>
      <p> 
      Freenet est un logiciel libre qui vous permet de partager des fichiers,
      naviguer dans et publier des "freesites" (sites web accessibles uniquement
      à travers Freenet), discuter sur des forums, le tout de façon anonyme et sans craindre
      la censure. Freenet est décentralisé pour le rendre moins vulnérable aux
      attaques, et s'il est utilisé en mode "darknet", où les utilisateurs se
      connectent uniquement à leurs amis, il est très difficile à détecter. 
      </p>
      
      <p>
      Les communications des nœuds Freenet sont chiffrées et routées à travers
      d'autres nœuds de façon à rendre extrêmement difficile de déterminer qui
      demande l'information, son contenu, et qui la fournit.
      </p>

      <p>
      Les utilisateurs contribuent au réseau en donnant de la bande passante
      et une partie de leur disque dur (appelée "datastore") pour y stocker
      des fichiers (ou des morceaux de fichiers) du réseau. Lorsque l'on demande
      un fichier disponible sur Freenet, on le reconstruit à partir des datastore
      des autres utilisateurs du réseau.<br/>
      Les fichiers sont gardés ou détruits de façon automatique suivant qu'ils
      sont populaires ou non. Le moins populaire est détruit pour laisser la place
      aux nouveaux fichiers ou aux contenus plus populaires.<br/>
      Les fichiers sont chiffrés, et les utilisateurs ne peuvent donc pas
      découvrir facilement quel est le contenu de son datastore, et 
      devrait ne pas pouvoir en être tenu responsable.<br/>
      Les forums de discussion, les sites, et la fonction de recherche, sont
      tous construits à partir de ce datastore distribué.
      </p>
      <p>
      Freenet a été téléchargé plus de 2 millions de fois depuis que le projet
      a débuté, et a été utilisé pour la diffusion partout dans le monde 
      d'informations censurées, y compris dans les pays comme la Chine et
      le Moyen Orient.
      Les idées et concepts précurseurs de Freenet ont eu un impact significatif
      dans le milieu académique. Notre papier de 2000 intitulé "Freenet: A Distributed
      Anonymous Information Storage and Retrieval System" (Freenet : un système de stockage
      et de récupération d'information anonyme et distribué) a été le papier
      de science informatique le plus cité de 2000 d'après <a href="http://citeseer.nj.nec.com/articles2000.html">Citeseer</a>, 
      et Freenet a aussi inspiré des papiers dans les mondes de la loi et de la 
      philosophie.<br/>
      Ian Clarke, le créateur de Freenet et le coordinateur du projet,
      a été sélectionné comme l'un des 100 plus grands innovateurs de 2003
      par le magazine "MIT's Technology Review".
      </p>
      
      <p>
      Le "darknet" est un développement récent, que peu d'autres réseaux possèdent :
      En se connectant uniquement aux personnes à qui ils font confiance, les 
      utilisateurs peuvent grandement améliorer leur sécurité, et ils peuvent 
      toujours se connecter au réseau principal à travers les amis des amis etc... de leurs amis.
      Ceci permet à certaines personnes d'utiliser Freenet dans des endroits où Freenet pourrait
      être illégal, rend Freenet très difficile à bloquer par les gouvernements,
      et ne se base pas sur le fait d'accéder au "monde libre" via un portail sécurisé.
      </p>
      
