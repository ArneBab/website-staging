	<h1>Les parrains de Freenet</h1>
	
	<p>
	Les personnes, organisations et compagnies suivantes ont généreusement
	parrainés ce projet grâce à leur don ou aux réductions sur le matériel
	et les services :
	</p>
	<ul>
	  <li>
	    <a href="http://onemansblog.com/">John Pozadzides</a><br/>
	    John est le fondateur de HTMLHelp.com et l'anvien
	    Vice Président des Ventes de SAVVIS Communications.
	    John a donnée 10 000$ au projet Freenet pour financer
	    le développement du logiciel Freenet.
	  </li>
	  
	  <li>
	    <a href="http://www.toad.com/gnu/">John Gilmore</a><br/>
	    John Gilmore est l'un des fondateurs de l'Electronic
	    Frontier Foundation, de la liste de diffusion Cypherpunks,
	    et de Cygnus Solutions. Il a crééé la hiérarchie alt.*
	    de Usenet, et est un contributeur clé au projet GNU.
	    Il a donné 10 000$.
	  </li>
	  
	  <li>
	    <a href="http://bytemark.co.uk/">Bytemark Hosting</a><br/>
	    Bytemark fourni des tarifs d'hébergement préférentiels pour Freenet
	    et nous les recommandons vivement.
	  </li>
	  
	  <li>
	    <a href="http://www.zoozle.org/">zoozle Torrent Search</a><br/>
	    zoozle est un moteur de recherche pour BitTorrent et les réseaux de P2P.
	    Il est également disponible en <a href="http://www.zoozle.net/"
			    title="deutsche Torrent Suchmaschine">allemand</a>
	    et en <a href="http://www.zoozle.biz/" title="le moteur
							français de recherche pour BitTorrent">français</a>.
	    Ils ont donnés 1 500$.
	  </li>
	  
	  <li>
	    <a href="http://www.yourkit.com/">YourKit, LLC</a><br/>
	    YourKit soutient aimablement Freenet en nous fournissant une
	    licence gratuite de leur profileur Java riche en fonctionnalités.
	    YourKit, LLC est le créateur d'outils innovants et intelligents
	    pour profiler des applications Java. Vous pouvez en apprendre
	    d'avantage sur leur profileur <a href="http://www.yourkit.com/java/profiler/index.jsp">YourKit
	      Java Profiler</a> en suivant le lien.
	  </li>
	  
	  <li>
	    <a href="http://www.google.com/">Google</a><br/>
	    Google <a href="http://code.google.com/">open source</a> a donné deux fois 18 000$&nbsp;
	    ils ont aussi payé les étudiants qui ont travaillé avec nous l'été pendant quatre
	    ans au travers de leur programme <a href="http://code.google.com/soc/">Summer of Code</a>.
	  </li>
	  
	  <li>
	    <a href="http://www.finanzvergleich.de">Finanzvergleich</a><br/> 
	    Finanzvergleich est un site allemand de comparaison de 
	    services financiers, qui a contribué à hauteur de 1 500$.
	  </li>
	  
	  <li>
	    <a href="http://www.topdir.de" title="Top Directory">TopDir</a><br/>
	    TopDir est un répertoire web édité manuellement qui contient
	    des informations à propos de sujets et sites web intéressants,
	    le tout en allemand.
	  </li>
	  
	</ul>

	<p>
	Si vous souhaitez vous aussi parrainer Freenet, merci de contacter <a href="mailto:ian@locut.us">Ian
	    Clarke</a>
	</p>
	
