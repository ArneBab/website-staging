      <h4>Pour le projet Freenet a-t-il besoin de dons ?</h4>
      <p>
      Bien que de nombreuses personnes ont donné de leur temps et de leurs compétences au 
      projet depuis sa conception, le projet Freenet repose sur vos dons pour d'une part
      payer un développeur travaillant à temps plein sur ce projet d'une complexité croissante,
      et d'autre part pour financer le matériel (le serveur) et les frais administratifs 
      (le nom de domaine). Nous aimerions aussi mettre en place un fond en cas d'attaque
      judiciaire.
      </p>
      <p>
      Toutes les personnes impliquées sont vraiment intéressées par le futur du projet
      Freenet et vous pouvez être assuré que les dons ont été, et seront, utilisés sagement.
      Tout type de soutien est très apprécié.
      </p>
      
