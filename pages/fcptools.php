<h2>What are the FCPTools?

</h2>
<br/>The most useful component of the FCPTools is ezFCPlib, a portable ANSI-C based Freenet Client Library. It's been written to make writing Freenet clients as easy as possible.
<br/>
<br/>In addition, the FCPTools include command line utilities to insert and retrieve files (fcpput, fcpputsplit, fcpget) as well as entire freesites (fcpputsite) via ezFCPlib.
<br/>
<br/><hr/><h2>Contents of FCPTools:
</h2>
<br/><b>ezFCPlib</b> - the engine that drives the tools. Draft API doc included under ./ezFCPlib/doc.
<br/>
<br/><b>fcpget</b> - a key request utility.
<br/>
<br/><b>fcpput</b> - a key insert utility.

<br/>
<br/><b>fcpputsite</b> - a freesite insertion utility.
<br/>
<br/><b>fcpputsplit</b> - a splitfile insertion utility.
<br/>
