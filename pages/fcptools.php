<br/><hr/><h2>Download
</h2>
<br/><h3>Source
</h3>
<br/>Source tarballs are available from the <a class='wiki'  href='http://freenetproject.org/snapshots/'>snapshots directory</a>:

<br/>
<br/><a class='wiki'  href='http://freenetproject.org/snapshots/fcptools-unstable.tgz'>fcptools-unstable</a>: Latest version in CVS (mainly of interest to developers).
<br/><a class='wiki'  href='http://freenetproject.org/snapshots/fcptools-stable.tgz'>fcptools-stable</a>: Latest working version (for users and freesite publishers).
<br/>
<br/>Uncompress the source as follows:
<br/>
<br/><tt>jcl@nero:~$ tar xfz FILENAME.tgz</tt>
<br/>
<br/>This will unpack the source into a new directory. Change into this directory, and follow the instructions in the INSTALL.* and README files.
<br/>
<br/><h3>Binaries
</h3>
<br/><a class='wiki'  href='http://freenetproject.org/snapshots/fcptools-bin.zip'>Download pre-compiled Win32 binaries</a> based on the stable source tree only. If you are running Windows and want to try the unstable code, follow the instructions in INSTALL.msvc to compile the source yourself.

