<h3>News</h3>

<p><b>4th April, 2008 - Freenet Receives Donation Funding Ongoing Development Work</b></p>
<p>
	Google's <a href="http://code.google.com/opensource/">Open Source Team</a> has
	donated US$18,000 to the Freenet Project to support the ongoing
	development of the Freenet software.
</p>
<p>
	Freenet is a 501c3 non-profit corporation whose goal is the
	development of free software that allows users to publish and obtain
	information on the Internet without fear of censorship.
</p>
<p>
	Earlier this week Freenet launched the first release-candidate for
	Freenet 0.7, the 7th major iteration of Freenet since the project
	started in 1999.  Freenet 0.7's most important new feature is its
	ability to run in a "darknet" mode, to further protect the identities
	of Freenet users in countries which do not respect freedom of speech
	on the Internet.
</p>
<p>
	This donation will ensure the continued employment of Freenet's full-time 
	software developer, Matthew Toseland, so that we can continue
	to improve Freenet's performance, usability and security, which has already
	advanced significantly in 0.7.
</p>

<p><b>31st Mar, 2008 - Freenet 0.7.0 release candidate 1 now available</b></p>
<p>Freenet version 0.7 Release Candidate 1 is now available for public testing.</p>
<p>Freenet is a global peer-to-peer network designed to allow users to publish and 
consume information without fear of censorship.  To use it, you must download the 
Freenet software, available for Windows, Mac, Linux and other operating systems.  
Once you install and run Freenet, your computer will join a global, decentralized 
P2P network. You will be able to publish and consume information anonymously, either 
through your web browser, or through a variety of third party applications.</p>
<p>Freenet 0.7 is a ground-up rewrite of Freenet.  The key user-facing feature in 
Freenet 0.7 is the ability to operate Freenet in a "darknet" mode, where your Freenet 
node will only talk to other Freenet users that you trust.  This makes it much more 
difficult for an adversary to discover that you are using Freenet, let alone what 
you are doing with it. 0.7 also includes significant improvements to both security 
and performance.</p>
<p>Freenet 0.7 RC1 can be downloaded from:</p>
<p><a href="http://freenetproject.org/download.html">http://freenetproject.org/download.html</a></p>
