<h3>News</h3>
<p><b>17th Mar, 2008 - Freenet accepted into Google Summer of Code 2008</b></p>
<p>Google has accepted us into their <a href="http://code.google.com/soc/2008/">Summer of Code</a> 
for the third year. The last two years have been a mixed bag, but some good code has
come out of it (not least Thaw), and some good developers too. This year we will be doing
fewer projects than last year because we only have two confirmed mentors, and we will
be selecting candidates more carefully. If you want to get paid $4,500 by Google to 
work on Freenet over the summer, please have a look at the 
<a href="http://wiki.freenetproject.org/SummerOfCode2008">wiki page</a> (including ideas list, 
although feel free to suggest a proposal that isn't there)
and <a href="mailto:devl@freenetproject.org">contact us</a>.</p>

<p><b>29th Jan, 2008 - Freenet 0.7.0a2 alphatest released</b></p>
<p>Freenet version 0.7a2 is now available for public testing.</p>
<p>Freenet is a global peer-to-peer network designed to allow users to publish 
and consume information without fear of censorship.  To use it, you must download 
the Freenet software, available for Windows, Mac, Linux and other operating systems.  
Your computer will then form part of a global, decentralized P2P network, and you 
will be able to publish and consume information anonymously, either through your 
web browser, or through a variety of third party applications, such as Frost (see 
<a href="http://jtcfrost.sf.net/">http://jtcfrost.sf.net/</a>).</p>
<p>Since the first alpha there have been vast numbers of changes, in particular 
significant improvements in user friendliness.  For example, it is no-longer 
necessary to find other people to connect to, Freenet can find other people for you. 
However, you can still connect to your friends for maximum security. It is also a 
good deal faster, and many bugs have been fixed.</p>
<p>There have been many many other changes, and we will release a feature complete 
beta followed by 0.7.0 itself in the coming months (we hope to have regular point 
releases after that). But we thought it would be best to get some wider testing at 
this point. There is more to do before 0.7.0, and one or two features which need to 
be completed before that.</p>
<p>Freenet 0.7a2 can be downloaded from 
<a href="http://freenetproject.org/download.html">here</a>.</p>
<p>This release would not have been possible without the work of numerous 
volunteers, and Matthew Toseland, Freenet's full time developer. Matthew's work is 
funded through donations via our website (as well as a few larger sponsors from time 
to time), we ask that anyone who can help us to ensure Matthew's continued 
employment by visiting our donations page and making a contribution at 
<a href="/donate.html">the donation page</a>.</p>
<p><b>15th May, 2007 - John Pozadzides donates $10,000 to Freenet Project</b></p>
<p>
John Pozadzides, founder of <a href="http://HTMLHelp.com">HTMLHelp.com</a> and former Vice President of Sales for <a href="http://savvis.net">SAVVIS Communications</a>, has donated $10,000 to the Freenet Project to fund ongoing
development of the Freenet software.  John says:
</p>
<blockquote>
<p>
More than a decade ago when I founded HTMLHelp.com I had a vision of the Internet as the &#8220;great equalizer&#8221;. I believed that it would free us from the content filtering imposed by mega-media companies and governments. It turns out this has been true for most of us - but not all.
</p>
<p>
I view supporting the Freenet Project as a logical extension of that original dream, this time to include people who were previously unable to freely participate.
</p>
</blockquote>
<p>
This donation will be used to fund general development, and in particular, improvements to software usability and stability.  A specific
goal is the completion of Freenet's "opennet" functionality which will make it far easier for many users to get Freenet up and running.
</p>
<p>
Anyone is free to make their own donation to the project, via a variety of convenient methods including Paypal and Google Checkout through our <a href="donate.html">donations page</a>.
</p>
You can read John's blog entry announcing this donation <a href="http://onemansblog.com/2007/05/15/privacy-equals-freedom-support-the-freenet-project/">here</a>.
