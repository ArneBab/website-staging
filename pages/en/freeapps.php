<br/><a class='wiki'  href='http://lookoutpx.sourceforge.net/'><b>Lookout PX</b></a>
<br/>
<br/>Lookout PX is a modular, extendable Freenet client for Windows written in C++. Plug-ins are loaded dynamically and may extend virtually every aspect of Lookout. Currently under development is a transport-independent messaging subsystem, such that any messaging system (TOF, FMB, etc.) can be implemented for Lookout with a standard interface.
<br/>
<br/>Developers familiar with MFC, Win32 programming, or Freenet in general are wanted. Anyone is welcome to contact <a class='wiki'  href='mailto:delta407@invisiblenet.net'>delta407@invisiblenet.net</a> to get more information about the project.

<br/>
<br/>As of yet, there is no public release of Lookout, but a roadmap is on the table and a release -- a good release -- is approaching. Check <a class='wiki'  href='http://www.lookoutpx.net/'>http://www.lookoutpx.net/</a> for updates. (Plus, it is safe to expect there some kind of announcement on the freenet-tech list, in addition to informing C of E and a few others.)
