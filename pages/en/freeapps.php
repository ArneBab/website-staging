<br/><a class='wiki'  href='http://localhost:8888/SSK@d8i570BHEWgzYEGMjOsxRG5r0e4PAgM/NRAF//'><b>Not Really Anonymous Filesharing (NRAF)</b></a>  <b><span style='color:#FF0000;'>You must be running Freenet for this link to work</span></b>
<br/>
<br/>This is a proposal for a hybrid filesharing system. NRAF will use Freenet as a backbone for searching and listing shares, but the shared items will not be inserted. Transfers between users will be direct, and the users will not be anonymous.
<br/>
<br/>Why would anyone want such thing?
<br/>
<br/><ul><li> Thanks to intelligent routing of Freenet NRAF will be efficient for the users, becoming eventually more efficient than the other filesharing apps.

</li><li> It will also be efficient for Freenet, because the big mp3 and divx files will not be inserted.
</li><li> If NRAF gets popular more <b style="color:black;background-color:#ffff66">people</b> will be running nodes. Freenet in every home?
</li><li> Other filesharing apps aren't anonymous either
</li><li> It could be implemented as a plugin/module to an existing (and poupular) gnutella client.
</li></ul>
It is a win-win situation; those who want to share and leech get to do it more efficiently (even though not anonymously), and those who need anonymity for real reasons will have a more efficient Freenet, thanks to the larger number of nodes.
<br/>
<br/>And last but not least, if NRAF becomes popular, instead of blaming Freenet for facilitating copyright infringment and spreading kiddie porn the authorities will blame NRAF.
